						<h2>Billiary</h2>
						<p>
							Bile helps to aid digestion by breaking down the fats in the foods we eat. It is made in 
							the liver and stored in the gall bladder, and carried around our bodies by the bile ducts, 
							which connect the liver to the gall bladder. Together, the bile ducts and the gall bladder 
							are known as the biliary system.
						</p>
						<h4>Cancer of the biliary duct</h4>
						<p>
							Bile Duct cancer is not common, occurring in an estimated 1 in every 100,000 people. It 
							accounts for 10-15% of all primary liver cancer, and can affect both men and women. 
							Although it is rare, it is an aggressive cancer and in most cases it does not present 
							with symptoms until it is in its later stages.
						</p>
						<p>
							There are two main types of bile duct cancer; intrahepatic bile duct cancer which 
							originates from the part of the bile duct inside the liver, or extrahepatic bile duct 
							cancer which starts in the bile duct outside of the liver.
						</p>
						<p>
							Bile duct cancer is only curable if the cancerous cells have not spread, in which 
							case some of the bile duct will be removed. For those in the advanced stages of the 
							condition chemotherapy and in some cases, surgery is used to enhance the patientâ€™s 
							quality of life.
						</p>
						<h4>Treatment of biliary cancer</h4>
						<p>
							Treatment will be decided depending on the position and size of the cancer. 
							Deciding factors will also be based on if the cancer has spread beyond the bile 
							duct. In certain cases, the aim of treatment is to relieve symptoms rather than a cure.
						</p>
						<h4>Surgery</h4>
						<p>
							Surgery is the main form of treatment in the attempt to cure bile duct cancer. 
							The decision regarding whether surgery is possible and the type of operation 
							that may be undertaken as mentioned previously, depends on the size of the cancer
							 and whether it has spread into nearby tissues.
						</p>
						<h4>Removal of the bile ducts</h4>
						<p>
							If the cancer is at a very early stage only the bile ducts containing the cancer 
							are removed. The remaining ducts in the liver are then joined to the small bowel, 
							allowing the bile to flow again.
						</p>
						<h4>Partial liver resection</h4>
						<p>
							If it is found that the cancer has spread into the liver, the affected part of the 
							liver and the bile ducts are removed.
						</p>
						<h4>Whipple's procedure</h4>
						<p>
							If the cancer is larger and has spread into nearby structures, then the bile ducts, 
							part of the stomach, part of the small bowel (duodenum), the pancreas, gall bladder 
							and the surrounding lymph nodes are all removed. Following this type of surgery it 
							is likely patients will stay in intensive care for the first few days following the surgery.
						</p>
						<h4>Surgery to relieve blockage (obstruction)</h4>
						<p>
							The tumour may be difficult to remove, if this is the case an operation to help 
							relieve the blockage may be undertaken. Through having this surgery any jaundice will 
							subside. During surgery the Consultant will create a bypass of the blocked part of the 
							bile duct, the bile can then flow from the liver into the intestine.
						</p>

