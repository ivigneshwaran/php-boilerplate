						<h2>Bone Cancer</h2>
						<p>
							Bone is the supporting framework of your body. Most bones are hollow. 
							The outer part of bones is a network of fibrous tissue called matrix onto 
							which calcium salts are laid down.
						</p>
						<p>
							The hard outer layer of bones is made of compact (cortical) bone, which 
							covers the lighter spongy (trabecular) bone inside. The outside of the bone 
							is covered with a layer of fibrous tissue called periosteum. Some bones are 
							hollow and have a space called the medullary cavity which contains the soft 
							tissue called bone marrow (discussed below). The tissue lining the medullary 
							cavity is called endosteum. At each end of the bone is a zone of a softer form 
							of bone-like tissue called cartilage.
						</p>
						<p>
							Cartilage is softer than bone but more firm than most tissues. It is made of a 
							fibrous tissue matrix mixed with a gel-like substance that does not contain much calcium.
						</p>
						<p>
							Most bones start out as cartilage. The body then lays calcium down onto the 
							cartilage to form bone. After the bone is formed, some cartilage may remain at 
							the ends to act as a cushion between bones. This cartilage, along with ligaments 
							and some other tissues connect bones to form a joint. In adults, cartilage is 
							mainly found at the end of some bones as part of a joint. It is also seen at 
							the place in the chest where the ribs meet the sternum (breastbone) and in parts 
							of the face. The trachea (windpipe), larynx (voice box), and the outer part of 
							the ear are other structures that contain cartilage.
						</p>
						<p>
							Bone itself is very hard and strong. Some bone is able to support as much as 12,000 
							pounds per square inch. It takes as much as 1,200 to 1,800 pounds of pressure to break 
							the femur (thigh bone). The bone itself contains 2 kinds of cells. The osteoblast is 
							the cell that lays down new bone, and the osteoclast is the cell that dissolves old bone. 
							Bone often looks as if it doesn't change much, but the truth is that it is very active. 
							Throughout our bodies, new bone is always forming while old bone is dissolving.
						</p>
						<p>
							In some bones the marrow is only fatty tissue. The marrow in other bones is a 
							mixture of fat cells and blood-forming cells. The blood-forming cells produce red 
							blood cells, white blood cells, and blood platelets. Other cells in the marrow 
							include plasma cells, fibroblasts, and reticuloendothelial cells.
						</p>
						<p>Cells from any of these tissues can develop into a cancer.</p>
						<h4>Types of bone tumors</h4>
						<p>
							Most of the time when someone with cancer is told they have cancer in the bones, 
							the doctor is talking about a cancer that has spread to the bones from somewhere 
							else. This is called metastatic cancer. It can be seen in many different types of 
							advanced cancer, like breast cancer, prostate cancer, and lung cancer. When these 
							cancers in the bone are looked at under a microscope, they look like the tissue 
							they came from. For example, if someone has lung cancer that has spread to bone, 
							the cells of the cancer in the bone still look and act like lung cancer cells. 
							They do not look or act like bone cancer cells, even though they are in the bones. 
							Since these cancer cells still act like lung cancer cells, they still need to 
							be treated with drugs that are used for lung cancer.
						</p>
						<p>
							Other kinds of cancers that are sometimes called "bone cancers" start in the 
							blood forming cells of the bone marrow âˆ’ not in the bone itself. The most common 
							bone cancer is called multiple myeloma. Another cancer that starts in the bone 
							marrow is leukemia, but it is generally considered a blood cancer rather than a 
							bone cancer. Sometimes lymphomas, which more often start in lymph nodes, can 
							start in bone marrow. Multiple myeloma, lymphoma, and leukemia are not discussed 
							in this document. For more information on these cancers, refer to the individual 
							document for each.
						</p>
						<p>
							A primary bone tumor starts in the bone itself. True (or primary) bone cancers 
							are called sarcomas. Sarcomas are cancers that start in bone, muscle, fibrous 
							tissue, blood vessels, fat tissue, as well as some other tissues. They can develop 
							anywhere in the body.
						</p>
						<p>
							There are several different types of bone tumors. Their names are based on the 
							area of bone or surrounding tissue that is affected and the kind of cells forming 
							the tumor. Some primary bone tumors are benign (not cancerous), and others are 
							malignant (cancerous). Most bone cancers are sarcomas.
						</p>
						<h4>Benign bone tumors</h4>
						<p>
							Benign tumors do not spread to other tissues and organs and so are not usually 
							life threatening. They are generally cured by surgery. Types of benign bone tumors include:
						</p>
