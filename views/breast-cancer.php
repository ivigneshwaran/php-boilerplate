						<p>
							If you're a man who's just been diagnosed with breast cancer, you may feel shocked. 
							You have a serious disease and you have some important decisions to make. Perhaps 
							the kind of cancer you have makes it even more surprising. Hearing your doctor tell 
							you that you have breast cancer is probably not something you ever expected.
						</p>
						<p>
							You may also be frustrated as you try to find the facts about your 
							condition, because almost all of the information you come across is written 
							for women. If you're a man, you don't know what to expect
						</p>
						
						<h4>Recommended Related to Breast Cancer</h4>
						<p>
							Breast cancer in men and women is very similar, although men tend to get it at a 
							later age. The causes, the staging, and the treatments are similar. The prognosis 
							is different overall. Breast cancer in men is not usually considered as a 
							possibility and therefore, it is usually diagnosed at a later stage then 
							female breast cancers. Studies have demonstrated a worse prognosis for male 
							breast cancers as compared to female breast cancers, especially for African-American men.
						</p>
						<p>
							Fortunately, treatment for breast cancer has come a long way, for both men 
							and women. If you catch it at an early stage, your odds for recovery are excellent. 
							And even the most advanced cancers can usually be treated, possibly improving your 
							quality of life and letting you live longer.
						</p>
						<h4>Stage I</h4>
						<p>
							These cancers are still relatively small and either have not spread to the lymph nodes 
							(N0) or there is a tiny area of cancer spread in the sentinel lymph node (N1mi).
						</p>
						<p>
							The main treatment for stage I breast cancer is surgical removal of the cancer. Although 
							this is usually done by mastectomy, breast-conserving surgery such as a lumpectomy may also 
							be an option. But because there is very little breast tissue in men, usually the whole breast 
							(including the nipple) needs to be removed. If breast-conserving surgery is done, it is usually 
							followed by radiation therapy.
						</p>
						<p>
							The lymph nodes under the arm will be checked for cancer spread, either with an axillary 
							lymph node dissection (ALND) or sentinel node biopsy (SLNB). If the sentinel lymph node 
							contains cancer, a full ALND may be needed, depending on the size of the cancer in the 
							lymph node as well as what other treatment is planned.
						</p>
						<p>
							Hormone therapy and/or chemotherapy (chemo) may be recommended after surgery as 
							adjuvant therapy, based on the tumor size and results of lab tests. Adjuvant hormone 
							therapy is usually suggested for hormone receptor-positive tumors. Adjuvant chemo 
							is commonly used for tumors larger than 1 cm (about 1/2 inch) across and some smaller 
							tumors that may be more likely to spread (based on features such as grade or a high 
							growth rate). Men with HER2-positive tumors may also receive trastuzumab (Herceptin).
						</p>
						<h4>Stage II</h4>
						<p>
							These cancers are larger and/or have spread to a few nearby lymph nodes. One 
							option is to treat first with chemo and/or hormone therapy before surgery (neoadjuvant 
							therapy). For HER2-positive cancers, neoadjuvant therapy will likely include trastuzumab 
							and may also include pertuzumab (Perjeta). Then, as with stage I cancers, mastectomy 
							is usually done. The lymph nodes under the arm will be checked for cancer spread, 
							either with an ALND or SLNB. If the sentinel lymph node contains cancer, a full ALND 
							may be needed, depending on the size of the cancer in the lymph node as well as what 
							other treatment is planned.
						</p>
						<p>
							Radiation therapy may be given after surgery if the tumor is large or if it is 
							found to have spread to several lymph nodes. Radiation therapy lowers the risk of 
							the cancer coming back later (recurrence).
						</p>
						<p>
							Adjuvant hormone therapy is usually suggested for hormone receptor-positive 
							tumors. If neoadjuvant chemo wasn’t given, adjuvant chemo will likely be also 
							recommended. Choices about chemo may be influenced by a man's age and general 
							state of health. Men with HER2-positive cancer will probably also receive trastuzumab.
						</p>
						<h4>Stage III</h4>
						<p>
							This stage includes more advanced tumors (large or with growth into nearby skin 
							or muscle) and cancers with more lymph node involvement (either more underarm lymph 
							nodes containing cancer or lymph nodes inside the chest containing cancer).
						</p>
						<p>
							This stage is treated with surgery, usually mastectomy. SLNB may be done first, 
							but most patients with this stage need a full ALND. Radiation therapy is usually 
							recommended after surgery. Adjuvant hormone therapy (if the tumor is estrogen- or 
							progesterone-receptor positive) and chemo are usually recommended as well. Men 
							with HER2-positive cancers will probably also receive trastuzumab.
						</p>
						<p>
							Another option is to treat first with chemo and/or hormone therapy before 
							surgery (neoadjuvant therapy) to try to shrink the tumor. For HER2-positive 
							cancers, neoadjuvant therapy will likely include trastuzumab and may also include 
							pertuzumab. This is followed with mastectomy and ALND. Radiation is often given 
							after surgery. Adjuvant hormone therapy is given as well, if the cancer is hormone 
							receptor-positive (estrogen- or progesterone-receptor). Additional chemo may 
							also be given after surgery, depending on what was given before surgery and 
							how well the tumor responded.
						</p>
						<h4>Stage IV</h4>
						<p>
							Stage IV cancers have spread beyond the breast and nearby lymph nodes to other 
							parts of the body. Breast cancer most commonly spreads to the bones, liver, and lungs. 
							As the cancer progresses, it may spread to the brain, but it can affect any organ and 
							tissue, even the eyes.
						</p>
						<p>
							While surgery and/or radiation may be useful in some situations (see below), 
							systemic therapy is the main treatment. Depending on many factors, this may be 
							hormone therapy, chemo, targeted therapy, or some combination of these treatments. 
							Targeted therapy options include trastuzumab, trastuzumab plus pertuzumab (Perjeta), 
							ado-trastuzumab emtansine (Kadcyla), and lapatinib.
						</p>
						<p>
							If you’d like information on a drug used in your treatment or a specific drug 
							mentioned in this section, see our Guide to Cancer Drugs , or call us with the names 
							of the medicines you’re taking.
						</p>
						<p>
							All of the systemic therapies given for breast cancer — hormone therapy, chemo, 
							and targeted therapies — have potential side effects, which were described in previous 
							sections. Your doctor will explain to you the benefits and risks of these treatments 
							before prescribing them.
						</p>
						<p>Radiation therapy and/or surgery may also be used in certain situations, such as:</p>
						<ul>
							<li>Radiation therapy and/or surgery may also be used in certain situations, such as</li>
							<li>To treat a small number of metastases in a certain area</li>
							<li>To prevent bone fractures</li>
							<li>When an area of cancer spread is pressing on the spinal cord</li>
							<li>To treat a blockage in the liver</li>
							<li>To relieve pain or other symptoms</li>
							<li>When the cancer has spread to the brain</li>
						</ul>
						<p>
							In some cases, regional chemo (where drugs are delivered directly into a certain 
							area, such as the fluid around the brain or into the liver) may be useful as well.
						</p>
						<p>
							Treatment to relieve symptoms depends on where the cancer has spread. For example, 
							pain from bone metastases may be treated with external beam radiation therapy and/or 
							bisphosphonates or denosumab (Xgeva). Most doctors recommend bisphosphonates or denosumab 
							along with calcium and vitamin D for all patients whose breast cancer has spread to their bones.
						</p>

