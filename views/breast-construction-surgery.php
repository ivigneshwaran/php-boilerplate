						<h2>Breast Reconstruction Surgery</h2>
						<p>
							Breast reconstruction is a type of surgery for women who have had a breast removed. 
							Here you can find information to help you better understand your options if you are 
							considering breast reconstruction.
						</p>
						<p>
							Breast reconstruction is a type of surgery for women who have had all or part 
							of a breast removed. The surgery rebuilds the breast mound so that it is about 
							the same size and shape as it was before. The nipple and the darker area around 
							the nipple (areola) can also be added. Most women who have had a breast removed 
							(mastectomy) can have reconstruction. Women who have had only the part of the 
							breast around the cancer removed (lumpectomy) may not need reconstruction. 
							Breast reconstruction is done by a plastic surgeon.
						</p>
						<h4>Types of breast reconstruction</h4>
						<p>
							Several types of operations can be used to reconstruct your breast. You can have 
							a newly shaped breast with the use of a breast implant, your own tissue flap, or a 
							combination of both. (A tissue flap is a section of your own skin, fat, and in some 
							cases muscle which is moved from your tummy, back, or other area of your body to the 
							chest area.)
						</p>
						<h4>Tissue flap procedures</h4>
						<p>
							These procedures use tissue from your tummy, back, thighs, or buttocks to rebuild 
							the breast. The 2 most common types of tissue flap procedures are the TRAM flap (or 
							transverse rectus abdominis muscle flap), which uses tissue from the lower tummy area, 
							and the latissimus dorsi flap, which uses tissue from the upper back. Other tissue 
							flap surgeries described below are more specialized, and may not be available everywhere.
						</p>
						<p>
							These operations leave 2 surgical sites and scars — one where the tissue was 
							taken and one on the reconstructed breast. The scars fade over time, but they will 
							never go away completely. There can be donor site problems such as abdominal hernias 
							and muscle damage or weakness. There can also be differences in the size and shape 
							of the breasts. Because healthy blood vessels are needed for the tissue’s blood 
							supply, flap procedures are not usually offered to women with diabetes, connective 
							tissue or vascular disease, or to smokers.
						</p>
						<p>
							In general, flap procedures behave more like the rest of your body tissue. For 
							instance, they may enlarge or shrink as you gain or lose weight. There’s also no worry 
							about replacement or rupture.
						</p>
						<h4>TRAM (transverse rectus abdominis muscle) flap</h4>
						<p>
							The TRAM flap procedure uses tissue and muscle from the tummy (the lower abdominal wall). 
							The tissue from this area alone is often enough to shape the breast, so that an implant 
							may not be needed. The skin, fat, blood vessels, and at least one abdominal muscle are 
							moved from the belly (abdomen) to the chest. The TRAM flap can decrease the strength 
							in your belly, and may not be possible in women who have had abdominal tissue removed 
							in previous surgeries. The procedure also results in a tightening of the lower belly, 
							or a “tummy tuck.”
						</p>
						<h4>There are 2 types of TRAM flaps:</h4>
						<p>
							A pedicle flap leaves the flap attached to its original blood supply and tunnels it 
							under the skin to the breast area. This can leave an area of fullness under the skin 
							where the tissue is tunneled.
						</p>
						<p>
							In a free flap, the surgeon cuts the flap of skin, fat, blood vessels, and muscle 
							for the implant free from its original location and then attaches it to blood vessels 
							in the chest. This requires the use of a microscope (microsurgery) to connect the 
							tiny vessels and takes longer than a pedicle flap. The free flap is not done as 
							often as the pedicle flap, but some doctors think that it can result in a more 
							natural shape.
						</p>
						<p>
							The latissimus dorsi flap moves muscle and skin from your upper back when extra 
							tissue is needed. The flap is made up of skin, fat, muscle, and blood vessels. It’s 
							tunneled under the skin to the front of the chest. This creates a pocket for an 
							implant, which can be used for added fullness to the reconstructed breast. Though 
							it’s not common, some women may have weakness in their back, shoulder, or arm 
							after this surgery.
						</p>
						<h4>DIEP (deep inferior epigastric artery perforator) flap</h4>
						<p>
							The DIEP flap uses fat and skin from the same area as in the TRAM flap but does 
							not use the muscle to form the breast mound. This results in less skin and fat in 
							the lower belly (abdomen), or a “tummy tuck.” This method uses a free flap, meaning 
							that the tissue is completely cut free from the tummy and then moved to the chest 
							area. Use of a microscope (microsurgery) is needed to connect the tiny vessels. The 
							procedure takes longer than the TRAM pedicle flap discussed above, but leaves less 
							muscle weakness and causes fewer hernias. It isn’t available in all areas.
						</p>
						<h4>Gluteal free flap</h4>
						<p>
							The gluteal free flap or GAP (gluteal artery perforator) flap is newer 
							type of surgery that uses tissue from the buttocks, including the gluteal 
							muscle, to create the breast shape. It might be an option for women who 
							cannot or do not wish to use the tummy sites due to thinness, incisions, 
							failed tummy flap, or other reasons, but it’s not offered in many areas 
							of the country. The method is much like the free TRAM flap mentioned above. 
							The skin, fat, blood vessels, and muscle are cut out of the buttocks and 
							then moved to the chest area. A microscope (microsurgery) is needed to 
							connect the tiny vessels.
						</p>
						

