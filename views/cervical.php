						<h2>Cervical Cancer</h2>
						<p>
							The common early symptom of cervical cancer (cancer of the cervix) is abnormal vaginal bleeding. 
							Most cases develop in women in their 30s or 40s. If cervical cancer is diagnosed at an early 
							stage, there is a good chance of a cure.
						</p>
						<h4>What is the cervix?</h4>
						<p>
							The cervix is the lower part of the womb (uterus) which extends slightly into the 
							top of the vagina. The cervix is often called the neck of the womb.
						</p>
						<p>
							A narrow passage called the cervical canal (or endocervical canal) goes from the vagina to 
							the inside of the womb. This is normally kept tightly shut, but allows blood to flow out 
							from the uterus during a period, and sperm to travel inside when you have sex. It opens 
							very wide during labour when you have a baby. The surface of the cervix is covered with 
							skin-like cells. There are also some tiny glands in the lining of cervical canal which make mucus.
						</p>
						<h4>What is cervical cancer?</h4>
						<p>There are two main types of cervical cancer:</p>
						<p>
							Squamous cell cervical cancer is the most common. This develops from a skin-like 
							cell (a squamous cell) that covers the cervix which becomes cancerous.
						</p>
						<p>
							Adenocarcinoma cervical cancer is less common. This develops from a glandular cell 
							(a cell that makes mucus) within the cervical canal which becomes cancerous.
						</p>
						<p>Both types are diagnosed and treated in a similar way.</p>
						<h4>Who gets cervical cancer?</h4>
						<p>
							Most cases develop in women aged in their 30s or 40s. Some cases develop in older and younger 
							women. It is rare in women aged under 25 years.
						</p>
						<p>
							Cervical cancer is the twelfth most common type of cancer in women in the UK. 
							Around 950 women in the UK die from this cancer every year. However, the number 
							of cases diagnosed each year has fallen over recent years. This is because cervical 
							cancer can be prevented by regular cervical screening tests.
						</p>
						<h4>What causes cervical cancer?</h4>
						<p>
							A cancerous tumour starts from one cell. It is thought that something damages 
							or alters certain genes in the cell. This makes the cell very abnormal and 
							multiply out of control. (See separate leaflet called What Causes Cancer for more details.)
						</p>
						<p>
							In the case of cervical cancer, the cancer develops from a cell which is already abnormal 
							- see above. In most cases, abnormal cells are present for years before one of the abnormal 
							cells becomes cancerous and starts to multiply out of control into a cancerous tumour. 
							The initial pre-cancerous abnormality of cervical cells is usually caused by a prior 
							infection with the human papillomavirus (HPV).
						</p>

