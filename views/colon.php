						<h2>Colon Cancer</h2>
						<p>
							The colon is the longest part of the large intestine and the lowest part of the digestive system. 
							Inside the colon, water and salt from solid wastes are extracted before the waste moves through 
							the rectum and exits the body through the anus.
						</p>
						<p>
							Cancer is a class of diseases characterized by out-of-control cell growth, and colon cancer forms 
							when this uncontrolled cell growth initiates with cells in the large intestine. Most colon cancers 
							originate from small, noncancerous (benign) tumors called adenomatous polyps that form on the 
							inner walls of the large intestine. Some of these polyps may grow into malignant colon cancers 
							over time if they are not removed during colonoscopy. Colon cancer cells will invade and damage 
							healthy tissue that is near the tumor causing many complications.
						</p>
						<p>
							After malignant tumors form, the cancerous cells may travel through the blood and lymph 
							systems, spreading to other parts of the body. These cancer cells can grow in several places, 
							invading and destroying other healthy tissues throughout the body. This process itself is 
							called metastasis, and the result is a more serious condition that is very difficult to treat.
						</p>
						<p>
							Colon cancer is not necessarily the same as rectal cancer, but they often occur together in 
							what is called colorectal cancer. Rectal cancer originates in the rectum, which is the last 
							several inches of the large intestine, closest to the anus.
						</p>
						<h4>causes colon cancer</h4>
						<p>
							Cancer is ultimately the result of cells that uncontrollably grow and do not die. 
							Normal cells in the body follow an orderly path of growth, division, and death. Programmed 
							cell death is called apoptosis, and when this process breaks down, cancer results. Colon 
							cancer cells do not experience programmatic death, but instead continue to grow and divide. 
							Although scientists do not know exactly what causes these cells to behave this way, 
							they have identified several potential risk factors.
						</p>
						<p>
							Colon cancer usually derives from precancerous polyps that exist in the large intestine. 
							The most common types of polyps are:
						</p>
						<ul>
							<li>Adenomas: can become cancerous but are usually removed during colonoscopy</li>
							<li>Hyperplastic polyps: rarely become colon cancer</li>
							<li>Inflammatory polyps: usually occur after inflammation of the colon (colitis) and may become cancerous</li>
						</ul>
						<h4>Genes - the DNA type</h4>
						<p>
							Cells can experience uncontrolled growth if there are damages or mutations to DNA, 
							and therefore, damage to the genes involved in cell division. Four key types of genes 
							are responsible for the cell division process: oncogenes tell cells when to divide, 
							tumor suppressor genes tell cells when not to divide, suicide genes control apoptosis 
							and tell cells to kill themselves if something goes wrong, and DNA-repair genes instruct 
							cells to repair damaged DNA.
						</p>
						<p>
							Cancer occurs when a cell's gene mutations make the cell unable to correct DNA damage and 
							unable to commit suicide. Similarly, cancer is a result of mutations that inhibit oncogene 
							and tumor suppressor gene functions, leading to uncontrollable cell growth. If you have DNA
							mutations of oncogenes or tumor suppressor genes that lead to colon cancer, it is likely 
							that the mutation was a result of factors that affect DNA after you were born rather than 
							a result of inheritance from parents.
						</p>
						<h4>symptoms of colon cancer</h4>
						<p>
							Cancer symptoms are quite varied and depend on where the cancer is located, where it 
							has spread, and how big the tumor is. It is common for people with colon cancer to 
							experience no symptoms in the earliest stages of the disease. However, when the cancer 
							grows, symptoms include:
						</p>
						<ul>
							<li>Diarrhea or constipation</li>
							<li>Changes in stool consistency</li>
							<li>Narrow stools</li>
							<li>Rectal bleeding or blood in the stool</li>
							<li>Pain, cramps, or gas in the abdomen</li>
							<li>Pain during bowel movements</li>
							<li>Continual urges to defecate</li>
							<li>Weakness or fatigue</li>
							<li>Unexplained weight loss</li>
							<li>Irritable bowel syndrome (IBS)</li>
							<li>Iron deficiency (anemia)</li>
						</ul>
						<p>
							If the cancer spreads, or metastasizes, additional symptoms can present themselves 
							in the newly affected area. Symptoms of metastasis ultimately depend on the location 
							to which the cancer has spread, and the liver is the most common place of metastasis.
						</p>
						<h4>How is colon cancer diagnosed and staged?</h4>
						<p>
							In order to diagnose colon cancer, physicians will request a complete physical exam as 
							well as personal and family medical histories. Diagnoses are usually made after the 
							physician conducts a colonoscopy or a barium enema x-ray (lower G series).
						</p>
						<p>
							A colonoscopy is a procedure where a long, flexible tube with a camera on one end is 
							inserted into the rectum to inspect the inside of the colon. If polyps are found in the 
							colon, they are removed and sent to a pathologist for biopsy - an examination under a 
							microscope that is used to detect cancerous or precancerous cells. A barium enema begins 
							with the patient swallowing a special liquid that contains the element barium. After 
							the barium lines the large intestine, an x-ray of the colon and rectum is taken. Tumors 
							and polyps will appear as dark shadows on the x-ray.
						</p>
						<p>
							If a colon cancer diagnosis is made after a biopsy, doctors will often order chest x-rays, 
							ultrasounds, or CT scans of the lungs, liver, and abdomens to see how far the cancer has 
							spread. It is also not uncommon for a doctor to test blood for CEA (carcinoembyonic antigen) 
							- a substance produced by some cancer cells.
						</p>
						<p>
							After a diagnosis is made, doctors determine the stage of the cancer. The stage determines 
							which choices will be available for treatment and informs prognoses. The standard cancer 
							staging method is called the TNM system. T indicates the size and direct extent of the 
							primary tumor or degree of invasion into the intestinal wall, N indicates the degree to 
							which the cancer has spread to nearby lymph nodes, and M indicates whether the cancer 
							has metastasized to other organs in the body. A small tumor that has not spread to lymph 
							nodes or distant organs may be staged as (T1, N0, M0), for example.
						</p>
						<p>
							Colon cancer is also staged from 0 to IV, derived from the TNM classification. Stage 0 is 
							written as (Tis, N0, M0) where Tis stands for carcinoma in situ. This is when the tumor has 
							not grown beyond the inner layer of the colon or rectum and has not invaded deeper tissues 
							nor spread outside of the colon. Stage IV is written as (Any T, Any N, M1) and describes 
							cancer that has spread to distant sites and other organs throughout the body.
						</p>
