						<h2>Colorectal Cancer</h2>
						<p>
							In order to understand colon and rectal cancer, collectively known as colorectal cancer, 
							it might first help to understand what parts of the body are affected and how they work.
						</p>
						<h4>The Colon</h4>
						<p>
							The colon is a 6-foot long muscular tube connecting the small intestine to the rectum. 
							The colon, which along with the rectum is called the large intestine, is a highly specialized 
							organ that is responsible for processing waste so that emptying the bowels is easy and 
							convenient. The colon removes water from the stool, and stores the solid stool. Once or 
							twice a day it empties its contents into the rectum to begin the process of elimination.
						</p>
						<h4>The Rectum</h4>
						<p>
							The rectum is an 8-inch chamber that connects the colon to the anus. It is the rectum's 
							job to receive stool from the colon, to let you know that there is stool to be evacuated, 
							and to hold the stool until evacuation happens.
						</p>
						<h4>Colorectal Cancer</h4>
						<p>
							Cancer that begins in the colon is called colon cancer, and cancer that begins in the 
							rectum is called rectal cancer. Cancers affecting either of these organs also may be 
							called colorectal cancer.
						</p>
						<p>
							Colorectal cancer occurs when some of the cells that line the colon or the rectum become 
							abnormal and grow out of control. The abnormal growing cells create a tumor, which is the cancer.
						</p>
						<h4>Stages of Colorectal Cancer</h4>
						<p>
							Colon and rectal cancer are staged according to how far they have spread through the 
							walls of the colon and rectum and whether they have spread to other parts of the body. 
							This staging process allows doctors to determine the best treatments for the particular 
							cancer. It also allows them to determine if the cancer is getting better with treatment or not responding.
						</p>
						<h4>Stage 0 Colorectal Cancer</h4>
						<p>
							Stage 0 colorectal cancer is found only in the innermost lining of the colon. 
							Treatment usually involves one of the following,
						</p>
						<ul>
							<li>Polypectomy or local excision to remove the tumor and a small amount of surrounding tissue.</li>
							<li>More extensive surgery (resection) to remove larger colon cancers. This may require a 
								procedure called an anastomosis to remove the diseased part of the colon and reattach 
								the healthy tissue to maintain bowel function.</li>
							<li>Surgery to remove all of the cancer is considered curative</li>
						</ul>
						<h4>Stage I Colorectal Cancer</h4>
						<p>
							Stage I tumors have spread beyond the inner lining of the colon to the second and 
							third layers and involves the inside wall of the colon. The cancer has not spread to 
							the outer wall of the colon or outside the colon.
						</p>
						<p>
							Standard treatment involves surgery to remove the cancer and a small amount of tissue around 
							the tumor. Additional treatments are not usually needed.
						</p>
						<p>
							Aggressive surgery to remove all of the cancer offers a great potential for cure. The five-year 
							survival rate for stage I colorectal cancer is 93% according to the American Cancer Society.
						</p>
						<h4>Stage II Colorectal Cancer</h4>
						<p>
							Stage II colorectal cancers are larger and extend through the muscular wall of the colon, 
							but there is no cancer in the lymph nodes (small structures that are found throughout the 
							body that produce and store cells that fight infection).
						</p>
						<p>
							Standard treatment is surgical removal of the cancer and an area surrounding the cancer: 
							Chemotherapy may also be given as a precaution against cancer recurrence. This is usually 
							restricted to persons with high-risk disease, as the advantages of chemotherapy in this stage 
							of colon cancer are minimal. An oncologist should help in making the decision as to whether 
							chemotherapy is necessary for stage II colon cancers.
						</p>
						<h4>Stage III Colorectal Cancer</h4>
						<p>
							Stage III colorectal cancers have spread outside the colon to one or more lymph nodes 
							(small structures that are found throughout the body that produce and store cells that 
							fight infection). Tumors within the colon wall, which also involve the lymph nodes are 
							classified as stage IIIA, while tumors that have grown through the colon wall and have 
							spread to one to four lymph nodes are classified as stage IIIB cancers. Those tumors, 
							which have spread to more than four lymph nodes are classified as stage IIIC colon cancers.
						</p>
						<h4>Treatment involves:</h4>
						<ul>
							<li>Surgery to remove the tumor and all involved lymph nodes if possible.</li>
							<li>After surgery, the patient will receive chemotherapy with 5-FU, leucovorin and oxaliplatin, 
								capecitabine with oxaliplatin or capecitabine alone.</li>
							<li>Radiation may be needed if the tumor is large and invading the tissue surrounding the colon.</li>
							<li>The five-year survival rate for stage III colon cancer is about 64%. Patients with one to four 
							positive lymph nodes have a higher survival rate than people with more than five positive lymph nodes.</li>
						</ul>
						<h4>Stage IV Colorectal Cancer</h4>
						<p>
							Stage IV colorectal cancers have spread outside the colon to other parts of the body, such 
							as the liver or the lungs. Cancer that has spread is also called "metastatic." The tumor can 
							be any size and may or may not include affected lymph nodes (small structures that are found 
							throughout the body that produce and store cells that fight infection).
						</p>

