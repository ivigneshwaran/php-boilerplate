						<h2>Hyperthermic intraperitoneal chemotherapy (HIPEC)</h2>
						<p>
							Hyperthermic intraperitoneal chemotherapy (HIPEC) is a highly concentrated, 
							heated chemotherapy treatment delivered directly to the abdomen during surgery.
						</p>
						<p>
							Unlike systemic chemotherapy delivery, which circulates throughout the body via 
							the bloodstream, HIPEC delivers chemotherapy directly to cancer cells in the 
							abdomen. This allows for higher doses of chemotherapy treatment. Heating the 
							solution may also improve the absorption of chemotherapy drugs by tumors and 
							destroy microscopic cancer cells that remain in the abdomen after surgery.
						</p>
						<p>
							Before patients receive HIPEC treatment, doctors perform cytoreductive surgery—a 
							procedure to debulk, or reduce, the size of a cancerous tumor—within the abdomen. 
							When as many tumors as possible have been removed, a heated, sterilized chemotherapy 
							solution is delivered to the abdomen to penetrate and destroy remaining cancer cells. 
							The solution is 41 to 42 degrees Celsius, about the temperature of a warm bath. 
							It’s circulated throughout the abdomen for approximately 90 minutes. The solution 
							is then drained from the abdomen, and the incision is closed.
						</p>
						<p>
							HIPEC is a treatment option for people who have advanced surface spread of cancer 
							within the abdomen, but no cancer outside the abdomen.
						</p>
						<p>HIPEC</p>
						<ul>
							<li>Allows for high doses of chemotherapy.</li>
							<li>Enhances and concentrates chemotherapy within the abdomen.</li>
							<li>Reduces the rest of the body’s exposure to the chemotherapy.</li>
							<li>Improves chemotherapy absorption and susceptibility of cancer cells.</li>
							<li>Reduces some chemotherapy side effects.</li>
						</ul>
						<h4>How HIPEC Works?</h4>
						<p>
							During the HIPEC procedure, the surgeon will continuously circulate a heated 
							sterile solution–containing a chemotherapeutic agent–throughout the peritoneal 
							cavity, for a maximum of two hours. The HIPEC procedure is designed to attempt 
							to kill any remaining cancer cells.
						</p>
						<p>
							The procedure also improves drug absorption and effect with minimal exposure to 
							the rest of the body. In this way, the normal side effects of chemotherapy can be avoided.
						</p>
						<h4>Chemoembolization</h4>
						<p>
							Chemoembolization is an interventional radiology procedure designed to 
							reduce blood supply to a tumor and deliver chemotherapy drugs directly to the 
							tumor while reducing exposure to healthy tissues. This procedure reduces the 
							side effects of chemotherapy while killing cancer cells.
						</p>
						<p>
							Chemoembolization allows chemotherapy drugs to directly target the cancerous 
							tissue, in a higher concentration, for a longer period of time, reducing the 
							body’s exposure to the drugs.
						</p>
						<p>
							This method of chemotherapy is primarily used to treat liver cancer, and may 
							also be used to treat cancers that have spread to the liver.
						</p>
						<p>
							During chemoembolization, an interventional radiologist inserts a catheter into 
							an artery and guides the catheter to the tumors in the liver. Chemotherapy is 
							directly delivered into a tumor, through a catheter, using image guidance. The 
							chemotherapy drugs are released into the tumor, blocking the flow of blood to the tumor.
						</p>
						<h4>Intra-arterial chemotherapy (IAC)</h4>
						<p>
							Intra-arterial chemotherapy (IAC) delivers chemotherapy drugs directly to a 
							tumor, while reducing exposure to healthy tissues. This delivery method also 
							helps to reduce side effects, like nausea and vomiting, and enhance the drugs’ 
							cancer-killing properties.
						</p>
						<p>
							During IAC, a thin catheter is threaded through the femoral artery in the right leg. 
							Using image guidance, chemotherapy is injected directly into the hepatic artery, 
							which is the main blood vessel that delivers blood to the liver.
						</p>
						<p>
							IAC allows high doses of chemotherapy drugs to be targeted directly to the 
							cancerous tissue for a longer period of time, without exposing the body to 
							the effect of the drugs.
						</p>
						<p>
							Like chemoembolization, this method of chemotherapy is primarily used to 
							treat liver cancer, but may also be used to treat cancers that have spread to the liver.
						</p>
						<h4>Intraperitoneal chemotherapy</h4>
						<p>
							Intraperitoneal chemotherapy is a treatment sometimes used for ovarian 
							cancer, which often spreads into the abdominal cavity. This chemotherapy 
							technique delivers chemotherapy drugs directly into the abdominal cavity 
							through a catheter (thin tube).
						</p>
						<p>
							The catheter may be placed during staging surgery. If it is not inserted at 
							that time, it may be placed during a laparoscopic procedure. The catheter may 
							be connected to a port, which is a small, quarter-sized disc that sits just 
							under the skin and connects to a large vein. Chemotherapy medicines are 
							administered through a special needle that fits into the port. Blood may 
							also be drawn through the port, saving a patient multiple needle sticks.
						</p>
						<p>
							Intraperitoneal chemotherapy directly targets cancer cells in the abdomen, helping 
							to reduce drug exposure to healthy tissues. Intraperitoneal chemotherapy provides 
							a concentrated dose of drugs to the cancer cells in the abdominal cavity. The 
							chemotherapy is also absorbed into the bloodstream, reaching cancer cells outside 
							the abdomen. Higher doses of chemotherapy drugs may be administered intraperitoneally, 
							or directly into a body cavity, instead of intravenously, or into a vein.
						</p>
						<h4>Intrathecal chemotherapy</h4>
						<p>
							Intrathecal chemotherapy may be used to treat cancers that have reached the central 
							nervous system, including some types of leukemia and lymphoma.
						</p>
						<p>
							Typically, chemotherapy drugs do not pass through the blood-brain barrier. This 
							chemotherapy technique delivers chemotherapy drugs directly into the cerebrospinal fluid 
							surrounding the brain and spinal cord to target cancer cells that have spread there.
						</p>
						<p>
							Intrathecal chemotherapy is delivered through a lumbar puncture (spinal tap) or a device 
							placed under the scalp. Patients typically need to lie flat after the treatment to allow 
							the chemotherapy to reach all areas of the spinal cord and brain.
						</p>
						<h4>Symptoms</h4>
						<ul>
							<li>uncomfortable swelling of the abdomen</li>
							<li>loss of appetite and weight</li>
							<li>nausea and constipation</li>
							<li>Additionally, non-specific symptoms, such as tiredness and pain, may occur</li>
						</ul>
						
