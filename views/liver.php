						<h2>Liver</h2>
						<p>
							The liver is the largest organ in the body and is located in the upper right-hand 
							portion of the abdomen protected by the ribs.
						</p>
						<p>
							It lies beneath the diaphragm and on top of the stomach, right kidney and intestines. 
							The liver is dark, reddish-brown in colour and weighs approximately 1 and a half kilograms (3 lbs).
						</p>
						<p>
							It is divided naturally into two parts – the right and left lobes and it is held in position by 
							its connections to major blood vessels and other organs which lie close by. The liver is attached to the 
							diaphragm and other organs by banks of tissue (ligaments).
						</p>
						<p>
							The liver is the only organ in the body to have a double blood supply. Oxygenated blood flows in 
							from the hepatic artery and a nutrient-rich blood flows through the portal vein from the gut. 
							The blood drains from the liver through three hepatic veins (right, middle and left) directly 
							into the inferior vena cava. At any given moment, the liver holds about 23% of the body’s blood supply.
						</p>
						<p>
							At the very heart of your body, it carries out many complex functions – some of which are:
						</p>
						<ul>
							<li>Controlling the amount of cholesterol</li>
							<li>Filtering and cleaning your blood</li>
							<li>Fighting infection and disease</li>
							<li>Processing food once digested, it then produces bile which helps break down food in the gut</li>
							<li>Repairs damage and renews itself</li>
							<li>Cancer of the liver</li>
						</ul>
						<p>
							Malignant (cancerous) liver tumours can be either primary (the original site) or metastatic 
							(secondary to another cancer somewhere else in the body). Primary tumours which originate in the 
							liver can start from any of the different types of cells which normally exist in the liver.
						</p>
						<p>
							Some primary tumours in the liver are non-cancerous (benign) and do not spread to other parts of 
							the body they are usually small, causing no symptoms. Often discovered by chance during 
							operations or investigations for other conditions, unless they are causing symptoms, they do 
							not normally need to be removed.
						</p>
						<p>
						There are two types of primary liver cancer:
						</p>
						<p>
							Hepatocellular carcinoma (HCC) or Hepatoma arises from the main cells of the liver known as the 
							hepatocytes. Usually confined to the liver, it can occasionally spread to other organs. More common 
							in men than women, HCC occurrs mostly in people with cirrhosis of the liver.
						</p>
						<p>
							Cholangiocarcinoma (or bile duct cancer) starts in the cells lining the bile ducts and is more 
							common in women. (The bile ducts are the tubes connecting the liver and gall bladder to the small intestine).
						</p>
						<p>
							For gallbladder problems, London Bridge Hospital and 31 Old Broad Street have a One Stop Gallstone 
							Clinic to provide rapid advice, diagnosis and treatment please click here to download a leaflet.
						</p>
						<p>
							For those patients diagnosed with either Heptocellular cancer or Cholangiocarcinoma our experienced 
							team provides specialised and holistic care, using as appropriate all the diagnostic skills.
						</p>
						<h4>Treatment depends on the following</h4>
						<p>
							Tumour stage (how many nodules are present in the liver, how large they are, where 
							exactly are they located in the liver, has the tumour spread to surrounding lymph nodes or to distant organs.
						</p>
						<p>
							Condition of the liver itself (is it healthy or cirrhotic and if it is cirrhotic, how badly damaged is it?
						</p>
						<p>Age and overall fitness of the patient</p>
						<p>Surgical Management of Liver Metastases</p>
						<p>
							It is likely that your Consultant will recommend a combination of surgery along with chemotherapy 
							when treating liver cancer.
						</p>
						<p>
							The main risks of liver surgery are bleeding at the time of surgery, or bile leaks and liver failure after 
							the operation. The chances of a complication occurring are approximately 20-40%. Most of these complications 
							relate to infections e.g. in the chest, wound or infected collections of fluid in the abdomen which may 
							need to be drained under imaging guidance.
						</p>
						<p>
							The hospital may keep a patient in hospital for longer than usual (about 7-10 days usually).
						</p>
						<p>
							Liver failure occurs because of too little liver left behind after an operation (or a reasonable 
							amount of liver in a very large patient) or because the liver is of an appropriate size but 
							is already damaged by another disease process.
						</p>
						<p>
							This can be mild and require correction of a bleeding tendency (one of the many jobs of the liver is to 
							produce factors which help us stop bleeding) with plasma and vitamin K, may present as jaundice or yellow 
							discolouration of the tissues (most noticeable in the eyes) which can last a few weeks or may be 
							profound and life-threatening requiring admission to the intensive care unit.
						</p>
						<p>
							Bile leaks occur in approximately 5-15% of patients after liver surgery. The liver contains many small 
							bile tubes which are cut through at the time of surgery. Although the surgeon makes every effort to seal 
							these tubes, they may leak after the operation.
						</p>
						<p>
							If a bile leak occurs, the bile that has leaked may cause an infection. It needs to be drained 
							under imaging guidance and then efforts made to prevent any further bile leak to allow healing to 
							occur. This is carried out in either the endoscopy or X-ray Departments, who place a small 
							plastic tube (stent) into the bile duct. This cannot be felt and stays in for approximately six 
							weeks when it can be removed as a day case procedure.
						</p>
						<p>
							Bleeding is much less of a problem now that liver surgery has advanced.
						</p>
						<p>
							Liver surgery is only performed by a select group of Consultants with many years and numbers of 
							experience and many tools exist which help reduce blood loss. You may require a blood transfusion 
							however and should mention to your surgeon if you have religious or personal objections to blood 
							product use.
						</p>
						<p>
							If your liver is too small to allow a liver operation, your surgeon may refer you for a procedure 
							known as portal vein embolisation. In this procedure, the X-ray Department place a needle into 
							one of the liver blood vessels under sedation and local anaesthetic. The blood vessel to the 
							side of liver being removed is then blocked with special inert particles to cease blood flow. 
							The body immediately sends messages to the other side of the liver to grow. It takes about 
							six weeks for the liver to grow to an adequate size to allow safe surgery.
						</p>
						<p>
							It is an established procedure around the world and is generally painless and very safe. 
							The liver has a dual blood supply and therefore the liver that is “blocked” continues to 
							survive and does not get infected, although this is a risk. It does not always work but is 
							successful in approximately 80-90% of patients.
						</p>
						<p>
							After your operation your liver will regenerate or grow. You do not grow a new right liver if this 
							has been removed, rather a very large left liver results. It takes about six weeks for the liver to 
							grow but this continues for about a year. The resultant liver volume is usually very slightly 
							smaller than your original volume but this is not significant and your liver should function 
							completely normally after surgery with no dietary restrictions.
						</p>
						<p>
							Alcohol, unless the cause of your liver problem, is allowed after liver surgery. London Bridge 
							Hospital advises that if you drink, this is carried out responsibly and in moderation according 
							to government guidelines.
						</p>
						<h4>Treatment for HCC (Hepatocellular Carcinoma)</h4>
						<p>
							There are a number of treatments for HCC available to patients, depending on how extensive the 
							disease is both within the liver and if it has spread outside the liver, the patients general 
							health, in particular the condition of the patient’s background liver, and age.
						</p>
						<h4>The following treatment options are available:</h4>
						<h4>Surgery as a treatment for HCC (Hepatocellular Carcinoma)</h4>
						<p>
							Surgery is the most effective treatment for primary liver cancer, but this is not always possible 
							due to the size or position of the tumour. It is also not possible to operate if the cancer has 
							spread beyond the liver.
						</p>
						<p>
							If the liver is severely damaged by cirrhosis it may not be safe to have surgery.
						</p>
						<p>
							The liver has an outstanding ability to repair itself. Even if up to three-quarters of the liver is 
							removed it will start to re-grow very quickly, and may be back to normal size within a few weeks.
						</p>
						<p>
							Before any operation, you will have the opportunity to discuss this completely with your Consultant 
							so that you understand exactly what it involves. If only certain areas of the liver are affected by the 
							cancer and the rest of the liver is healthy, it may be possible to have an operation to remove the 
							affected part this is called a liver resection.
						</p>
						<h4>Liver transplantation as a treatment for HCC</h4>
						<p>
							Liver transplantation is recognised as the most effective potentially curative treatment.
						</p>
						<p>
							Removing the whole liver and replacing it with a liver from another person is a HCC treatment, 
							but can only be done in those cases when the tumour is small (less than 5cm) or there are fewer 
							than three tumours, all smaller than 3cm in size.
						</p>
						<h4>Loco-regional therapy as a HCC treatment</h4>
						<p>
							This is offered if:
						</p>
						<ul>
							<li>We are not able to offer you either liver surgery or transplantation</li>
							<li>As a first step in a treatment programme which will include liver surgery</li>
							<li>As a ‘holding treatment’ while you are on the waiting list for liver transplantation</li>
						</ul>
						<p>
							These procedures are not a cure for liver cancer. However, studies have shown that many patients 
							experience an improvement in symptoms and quality of life. They may also slow or halt further 
							growth of the tumour.
						</p>
						<h4>The therapies are:</h4>
						<h4>Trans-arterial chemo-embolisation (TACE)</h4>
						<p>
							Chemotherapy is delivered directly into the area of the liver that contains the cancer and this 
							is followed by the injection of a substance that blocks the small blood vessels feeding the 
							tumour (embolisation).
						</p>
						<p>
							Some patients are not able to tolerate the chemotherapy drug used in TACE, however, would benefit 
							from the embolisation of the blood vessels feeding the tumour. In this situation, we can also offer 
							embolisation without the chemotherapy drug Trans-arterial embolisation (TAE).
						</p>
						<h4>Radio-frequency ablation (RFA)</h4>
						<p>
							Radiofrequency ablation (RFA) is used to obliterate tumours of the liver. A low electrical current is 
							passed through the tumour tissue, generating heat so that the tumour cells die.
						</p>
						<h4>Sorafenib as a HCC Treatment</h4>
						<p>
							Sorafenib is a drug, taken as a tablet. It slows down the growth of cancer cells by stopping the 
							signals that tell the cancer cells to grow and multiply and it also helps to prevent the growth 
							of new blood vessels supplying the cancer. This will reduce the amount of oxygen and nutrients 
							the tumour can receive and interfere with its growth. In clinical trials it has been 
							demonstrated to improve outcomes and is the only systemic treatment licensed for HCC.
						</p>
						<h4>Systemic chemotherapy for treatment of HCC</h4>
						<p>
							Systemic chemotherapy, where you are given drugs which kill cancer cells via a drip in your arm, 
							is rarely used for the treatment of HCC as it has little effect on the disease.
						</p>
						<p>
							Radiotherapy may be used on occasion to relieve symptoms associated with secondary tumours.
						</p>


