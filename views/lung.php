						<h2>Lung Cancer</h2>
						<p>
							The lungs are complex organs; they lie on either side of your heart and fill the inside 
							of your chest. In an adult, each lung on average weighs about 1lb.
						</p>
						<p>
							However, the right lung is a little larger than the left because there is more room 
							inside the chest for it. The left lung has to share its space with the heart.
						</p>
						<p>
							There are two thin layers of tissue, called the pleura covering each lung. These 
							layers - or membranes - slide over each other as you breathe so that the lungs 
							can expand and contract.
						</p>
						<p>
							Both lungs are made up of lobes - three on the right and two on the left. The lungs 
							are a mass of fine tubes, the smallest of which end in tiny air sacs called the 
							alveoli. There are around 300 million of these alveoli.
						</p>
						<p>
							These alveoli have very thin walls. They are combined with the finest of blood 
							vessels called capillaries.
						</p>
						<p>
							The lungs are protected by the rib cage, between the ribs are muscles that are essential 
							for breathing. Below the lungs is your diaphragm. The diaphragm separates the chest from 
							the abdomen and is also involved in breathing.
						</p>
						<h4>Lung Cancer</h4>
						<p>
							Surgical resection is considered the best treatment when feasible in early-stage disease.
						</p>
						<p>
							Unfortunately, only 20 to 25% of patients can have an operation following a 
							diagnosis of lung cancer, the majority of patients being diagnosed with inoperable 
							cancer (spread) or being not fit for an operation.
						</p>
						<p>
							When surgery is not feasible, the cancer can be treated by radiotherapy (external 
							beam radiation), systemic therapy (chemotherapy and other drugs given orally or 
							intravenously at regular intervals) or both.
						</p>
						<p>
							Lung metastases (secondary cancer in the lung) are not uncommon in colon carcinoma, 
							renal cell carcinoma, malignant melanoma (skin cancer), breast carcinoma and sarcomas. 
							In selected cases, lung metastases can be removed surgically.
						</p>
						<p>
							New treatment modalities are available (radiofrequency ablation and cyberknife 
							radiosurgery) in selected cases.
						</p>
						<p>
							All tumours have to be staged before a final treatment plan can be agreed. This means 
							measuring the tumour, defining its exact position and relation to other thoracic 
							structures or organs and to find out if the tumour has spread into the regional lymph 
							nodes or into other organs such as the lung, liver or brain.Staging is performed 
							using scans, which may include CT and PET. Biopsy procedures might also be necessary, 
							including videothoracoscopy (VATS) or mediastinoscopy are the most commonly used 
							diagnostic procedures to obtain tissue from your tumour or lymph nodes if all other 
							techniques have failed to give a precise diagnosis. Most of these diagnostic 
							procedures can be performed as day-case procedures, or may involve an overnight stay in hospital.
						</p>
						<p>
							We may also need to assess your lung function, your cardiac function, fitness for surgery 
							and perform some rouotine blood tests.
						</p>
						<p>
							Following your initial staging, you may be referred directly for an operation (lung resection) 
							or for chemotherapy or/and radiotherapy.
						</p>
						<p>
							Alternatively, your Consultant may estimate that there is not enough information on your 
							tumour and may want to obtain more tissue or a more accurate diagnosis in order to 
							establish a therapeutic plan.
						</p>
						<h4>Treatment</h4>
						<p>
							Surgery is routinely performed as a treatment for primary lung cancer or secondary cancer 
							of the lung (metastases). Surgical procedures involve wedge resections of the lesions, 
							segmentectomy (removal of a segment of the lung, 1/10th of a lung usually), lobectomy 
							(removal of a lobe, 1/3rd to 1 half of a lung), bi-lobectomy (removal of two lobes) 
							or pneumonectomy (removal of the entire lung).
						</p>
						<p>
							Depending upon the extent and type of disease, surgery usually requires a minimum of four - 
							five days in hospital after a wedge resection or a lobectomy of lung and up to ten days 
							after a pneumonectomy (resection of left or right lung).
						</p>
						<p>
							Minimally invasive surgery as a lung cancer treatment is routinely performed at London 
							Bridge Hospital. You may be eligible for a VATS (keyhole surgery) wedge resection, VATS 
							segmentectomy (ablation of a segment of your lung) or a VATS lobectomy (removal of a 
							lobe of your lung) depending upon the size and location of the tumour.
						</p>
						<p>
							The possibility of having a minimally invasive procedure (keyhole surgery) will be 
							discussed with your Consultant. This procedure is appropriate if you have got early 
							stage lung cancer, with a tumour less than 2-3 cm in diameter and no positive lymph 
							nodes as assessed by PET-CT or mediastinoscopy.
						</p>
						<p>
							In that case, the lung lobe involved will be removed through a 5-6 cm minithoracotomy. 
							This has been shown to cause less post-operative pain than a conventional thoracotomy 
							and the recovery period is shorter. The in-hospital stay is usually three to four days 
							following a VATS lobectomy or segmentectomy.
						</p>
						<p>
							You may also have to discuss the possibility of having a paravertebral catheter or 
							an epidural catheter for adequate pain relief after your thoracotomy with one of 
							our Consultant Anaesthetists.
						</p>
						<p>
							Alternatively, you may have advanced stage lung cancer and the operation may 
							involve the resection of a great vessel, diaphragm, or part of the chest wall 
							together with part of your lung. In that case the procedure is considered carefully 
							and discussed with experienced radiologists and oncologists.
						</p>
						<p>
							At the present time, this kind of procedure is usually performed following a 
							course of induction chemotherapy to reduce the size of the tumour and make it 
							more easily resectible.
						</p>
						<p>
							We have a high dependency unit (HDU) and an intensive care unit (ICU) supervised by a 
							team of Consultant Critical Care Physicians allowing our team to perform complex cases. 
							Your stay in HDU or ICU will be determined by the extensiveness of your lung resection 
							and your co-morbidities (history of cardiac disease, diabetes, obesity, etc…).
						</p>
						<h4>Chemotherapy as a Lung Cancer Treatment</h4>
						<p>
							For many patients this is the mainstay of treatment. Chemotherapy is usually 
							administered as an outpatient treatment over three to four months and often 
							consists in a combination of two drugs given intravenously over a few hours. 
							Alternatively, your Consultant may recommend a new drug targeting cancer cell 
							receptors instead of the chemotherapy.
						</p>
						<p>
							If surgery is planned but the tumour is too large or is involving thoracic 
							organs other than the lung, your Consultant may recommend that you receive some 
							chemotherapy before the operation to help reduce the size of the tumour. Sometimes, 
							radiotherapy is administered along side chemotherapy to shrink the tumour further. 
							This is called induction treatment and this is increasingly used to make inoperable 
							tumours operable.
						</p>
						<p>
							Alternatively, your Consultant may recommend that you receive chemotherapy 
							following your operation. This treatment is usually started six - eight weeks 
							after surgery and is called adjuvant chemotherapy. This cancer treatment is 
							normally recommended to reduce the risk of cancer relapse.
						</p>
						<p>
							This treatment is recommended when the tumour is large, when the thoracic lymph 
							nodes are involved by the cancer, or when other thoracic organs are involved.
						</p>
						<h4>Radiotherapy as a Lung Cancer Treatment</h4>
						<p>
							Radiotherapy may be used to treat a lung tumour when surgery is not possible 
							due to high risk or multiple other medical problems. This treatment is usually 
							administered over a few days or weeks depending on the location of the tumour 
							and dose planned by your Consultant. Often it is combined with chemotherapy, 
							either at the same time (concurrently) or one after the other (sequentially).
						</p>
						<p>
							There are different ways to administer radiotherapy and new techniques and 
							advances in this field can now minimise the collateral damage to organs surrounding the tumour.
						</p>
						<p>
							Radiotherapy is rarely used before surgery. It is sometimes used following the 
							operation (six - eight weeks after) when the risk of local relapse is particularly 
							high. Alternatively it is useful to treat symptoms such as cough and pain, and 
							may be targeted at areas of cancer spread (such as bones) as well as the lung.
						</p>
						<p>
							Radiofrequency Ablation and Cyberknife Stereotactic Radiosurgery as a Lung Cancer Treatment.
						</p>
						<p>
							CT-guided radiofrequency ablation (RFA) and Cyberknife stereotactic radiosurgery 
							are new minimally invasive image-guided technologies available for patients who 
							cannot have surgery due to severe co-morbidities or poor lung function.
						</p>
						<p>
							These techniques are safe and their role is currently evaluated in the treatment 
							of early stage lung cancer and solitary lung metastases and in patients who are 
							not fit for surgical resection.
						</p>
						<p>
							Your treatment would be discussed and delivered by a team involving a Consultant 
							Radiologist, a Clinical Oncologist and a Thoracic Surgeon. Treatment planning is 
							mapped out on a computer with the aid of a PET-CT fusion scan to outline the 
							tumour that is to be targeted.
						</p>
						<p>
							If cyberknife treatment is the chosen treatment method, this is offered at 
							The Harley Street Clinic.
						</p>
						<p>
							RFA is mainly used to treat small lung tumours ( &#60 3cm ) or pulmonary metastases. 
							This is always done under general anaesthetic in the interventional radiology suite. 
							The procedure is performed by an Interventional Radiologist, with a Thoracic 
							Surgeon attending
						</p>
						<h4>Personalized Biomedicine, Biomarkers and Targeted Therapies</h4>
						<p>
							The landscape of thoracic oncology is changing. At present the treatment of 
							specific tumours such as lung cancer is based on large clinical trials (thousands of 
							patients) and the rule is of “one size fits all” with all patients with a similar cancer 
							type receiving the same drug is changing rapidly.
						</p>
						<p>
							Gene expression, profiling, genotyping and biomarkers are progressively becoming an 
							essential step in the analysis of thoracic cancers. In the past 10 years, new drugs 
							have been developed to target specific gene mutations and have transformed the prognosis 
							of lung cancer and other thoracic cancers.
						</p>
						<p>
							Once a diagnosis of lung cancer, malignant mesothelioma, tymoma or any other thoracic 
							cancer has been made, it is essential to find out which genetic abnormalities cause the 
							cell to grow uncontrollably. In lung cancer, genes such as EGFR and ALK are routinely 
							tested in many patients.
						</p>
						<p>
							No matter whether you have early-stage or advanced-stage lung cancer, the tissue analysis 
							will help your doctors decide which treatment is best for you.
						</p>
