					<h2>Neuroendocrine</h2>
					<p>
						The endocrine system is made up of cells that make hormones. Hormones are chemical 
						substances that have formed in the body, and are carried in the bloodstream. Hormones 
						have a very specific effect on other organs and cells in the body.
					</p>
					<h4>Neuroendocrine Tumour</h4>
					<p>
						Neuroendocrine tumours originate in the hormone-producing cells of the body’s 
						neuroendocrine system which consists of both endocrine cells (hormone-producing cells) 
						and nerve cells.
					</p>
					<p>
						Neuroendocrine cells can be found throughout the body, but are primarily found in the 
						lungs and gastrointestinal tract. The cells help to regulate the air and blood flow through 
						the lungs, and help to control the speed at which food travels through the gastrointestinal tract.
					</p>
					<p>
						Although they are uncommon, there are many types of neuroendocrine tumours which 
						can be either benign or malignant Some neuroendocrine tumours are associated with 
						specific symptoms due to the tumour producing hormones. However, 60% of cases are 
						diagnosed as neuroendocrine tumours rather than a specific cancer type, which means 
						a neuroendocrine tumours, can affect any number of body parts.
					</p>
					<p>
						Neuroendocrine tumours are most likely to affect people over the age of 60, and people 
						who suffer from a rare condition called Multiple Endocrine Neoplasia 1 have an increased 
						risk of developing a neuroendocrine tumour.
					</p>
					<p>
						There are many tests that can be used to diagnose a neuroendocrine tumour such as urine 
						tests, blood tests, an ultrasound scan, CT, MRI and biopsy.
					</p>
					<h4>Treatment</h4>
					<p>
						Treatment options and recommendations depend on several factors, including the type 
						and stage of neuroendocrine tumor, possible side effects, and the patient’s preferences 
						and overall health.
					</p>
					<h4>Surgery</h4>
					<p>
						Surgery is the main treatment for neuroendocrine tumours. This involves the removal of the 
						tumor, along with a small border of healthy tissue around the tumor, called a margin.
					</p>
					<p>
						In pheochromocytoma (a neuroendocrine tumor of the adrenal glands) laparoscopic surgery 
						may be performed. Laparoscopic surgery is a less invasive type of surgery that uses 
						three or four small incisions where a laparoscope (a thin, lighted tube) attached to 
						a video camera is inserted through one opening to guide the surgeon. Surgical instruments 
						are inserted through the other openings to perform the surgery.
					</p>
					<h4>Peptide Receptor Radiouclide Therapy (PRRT)</h4>
					<p>
						Peptide receptor radionuclide therapy (PRRT) combines octreotide with a radionuclide 
						(a radioactive substance) to form highly specialised molecules called radiolabeled somatostatin 
						analogues or radiopeptides.
					</p>
					<p>
						These radiopeptides can be injected into a patient and will travel throughout the 
						body binding to carcinoid tumor cells that have receptors for them.Once bound, 
						these radiopeptides emit radiation and kill the tumor cells theyare bound to.
					</p>
					<h4>Chemotherapy</h4>
					<p>
						Chemotherapy is the use of drugs to kill cancer cells, usually by stopping 
						the cancer cells’ ability to grow and divide.
					</p>
					<p>
						Systemic chemotherapy is delivered through the bloodstream to reach cancer cells throughout the body.
					</p>
					<p>
						The type of chemotherapy used will depend on the type of tumour. For high 
						grade neuroendocrine carcinomas this will normally be platinum/etoposide. 
						For more indolent tumours there are various options including streptozocin 
						& capecitabine and temozolomide & streptozocin.
					</p>
					<h4>Targeted therapy</h4>
					<p>
						Targeted therapy is a treatment that targets the cancer’s specific genes, proteins, 
						or the tissue. There are two targeted agents that may be used in the management of 
						neuroendocrine tumours and have licenses for pancreatic neuroendocrine tumours, 
						these are everolimus and sunitinib. These both have different mechanisms of action 
						and the patient may be offered both treatments at some point.
					</p>
					<h4>Hormone therapy</h4>
					<p>
						There is growing evidence that treatment with hormones, called octreotide 
						analogues, may slow the growth of NETs. In general this is very well tolerated 
						and may be suitable as initial treatment when an operation is not possible. In 
						addition, if the tumour produces hormones this treatment may reduce the impact 
						of the hormone production.
					</p>

