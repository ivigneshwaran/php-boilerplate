					<h2>Oesophalgeal/Stomach</h2>
					<p>
						Collectively oesophageal and stomach cancers are known as oesophago-gastric cancer, and 
						have become increasingly common over the past three decades.
					</p>
					<p>
						As with many cancers, the survival rate for oesophago-gastric cancer improves 
						dramatically with an early diagnosis.
					</p>
					<p>
						The oesophagus is the muscular tube that connects the throat to the stomach. 
						The wall of the oesophagus is made of several layers of tissue; cancers of the 
						oesophagus typically start in the inner layer of tissue and grow toward the outer 
						(surface) lining. When food is swallowed, the walls of the oesophagus contract 
						to move the food down the oesophagus to the stomach.
					</p>
					<p>
						The upper part of the oesophagus lies behind, but is separate from, the windpipe 
						(trachea). The windpipe connects your mouth and nose with your lungs, enabling us to breathe.
					</p>
					<p>
						The oesophagus has four layers:
					</p>
					<ul>
						<li>The mucosa - the inner layer or lining, which is moist to help food pass down into the stomach</li>
						<li>The submucosa - this contains glands that produce mucus (phlegm), which keeps the oesophagus moist</li>
						<li>The muscularis - the muscle layer, which pushes food down to the stomach</li>
						<li>The adventitia - the outer covering, which attaches the oesophagus to surrounding structures</li>
					</ul>
					<h4>Oesophagus Cancer</h4>
					<p>There are two main types of oesophageal cancer:</p>
					<p>
						<strong>Squamous cell carcinoma</strong> - this develops in the thin, flat cells of the 
						mucosa, which primarily line the upper part of the oesophagus.
					</p>
					<p>
						<strong>Adenocarcinoma</strong> - this develops in the glandular cells of the 
						submucosal lining of the oesophagus, which produce mucus.
					</p>
					<p>
						A cancer can occur anywhere along the length of the oesophagus. Squamous cell 
						cancers occur more commonly in the upper and middle regions. Adenocarcinomas tend to be 
						more common at the lower end, including the junction where the oesophagus joins the stomach.
					</p>
					<h4>The Stomach</h4>
					<p>
						The stomach is a muscular organ located on the left side of the upper abdomen. The 
						stomach receives food from the esophagus. As food reaches the end of the oesophagus, it 
						enters the stomach through a muscular valve called the lower oesophagus sphincter.
					</p>
					<p>
						The stomach secretes acid and enzymes that digest food. Ridges of muscle tissue 
						called rugae line the stomach. The stomach muscles contract periodically, churning food 
						to enhance digestion.
					</p>
					<p>
						The pyloric sphincter is a muscular valve that opens to allow food to pass from 
						the stomach to the small intestine.
					</p>
					<h4>Stomach Cancer</h4>
					<p>There are a number of rarer types of cancer that can affect the stomach</p>
					<p><strong>These include:</strong></p>
					<ul>
						<li>Soft tissue sarcomas, of which the most common are leiomyosarcomas and gastrointestinal stromal tumours (GISTs)</li>
						<li>Lymphomas such as mucosa associated lymphoid tissue (MALT) lymphomas</li>
						<li>Carcinoid tumours</li>
					</ul>
					<h4>Treatment</h4>
					<p>
						Part of the diagnostic testing process involves grading and “staging” of upper 
						GI cancers. It is at this stage your Consultant will determine the type of cancerous 
						cells or tumors present, the rate of tumor growth, and the degree to which the cancer 
						has spread to other organs and systems of the body.
					</p>
					<p>
						Your Consultant will then recommend a course of treatment that takes this information 
						into account, as well as your age, overall health, and personal and family circumstances, 
						and your unique needs and objectives for treatment.
					</p>
					<p>
						Surgery may be required to remove the tumor or tumors, the affected areas of the 
						oesophagus or stomach, and, in some cases, nearby lymph nodes. In particularly severe 
						cases, the entire stomach may need to be removed and alternative feeding procedures 
						are established. In partial removals of tissue and organs, the remaining portions of 
						the oesophagus and stomach are reattached to allow food to pass through the digestive system.
					</p>
					<p>
						Chemotherapy and internal or external radiation therapy may also be used alone, in 
						combination with each other, or in addition to surgical techniques.Oesophagus cancer 
						may respond to a form of laser therapy called photodynamic therapy (PDT) that targets 
						cancer cells that have been injected with certain types of chemicals that respond 
						to the light in a chemical reaction this then destroys the cancer cells.
					</p>
