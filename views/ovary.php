						<h2>Ovarian Cancer</h2>
						<p>
							Most cases of cancer of the ovary (ovarian cancer) develop in women over the age of 
							50. The cause is not clear. Some ovarian cancers can be cured. In general, the more 
							advanced the cancer (the more it has grown and spread), the less chance that it can be cured.
						</p>
						<h4>What are the ovaries?</h4>
						<p>
							Women have two ovaries, one on either side of the womb (uterus) in the pelvic area (lower 
							abdomen). Ovaries are small and round, each about the size of a walnut. The ovaries make 
							eggs. In fertile women, each month an egg (ovum) is released from one of the ovaries. 
							This is called ovulation. The egg passes down the Fallopian tube into the uterus where 
							it may be fertilised by a sperm.
						</p>
						<p>
							The ovaries also make hormones including the main female hormones - oestrogen and 
							progesterone. These hormones pass into the bloodstream and have various effects 
							on other parts of the body, including regulating the menstrual cycle and periods.
						</p>
						<h4>What is ovarian cancer and how common is it?</h4>
						<p>
							Epithelial ovarian cancer is the most common type (about 9 in 10 cases). This type of 
							cancer develops from one of the cells that surround the outside of each ovary. This 
							outer layer of cells is called the germinal epithelium of the ovary. Epithelial ovarian 
							cancer mainly affects women who have had their menopause - usually women aged over 
							50. It is rare in younger women. There are various subtypes depending on the exact 
							look of the cells causing the cancer (which can be seen under the microscope).
						</p>
						<p>
							Germ cell ovarian cancer develops from germ cells (the cells that make the eggs). 
							About 1 in 10 cases of ovarian cancer are germ cell cancers. They typically develop 
							in younger women. Again, there are various subtypes depending on the exact look of 
							the cells causing the cancer. Most cases of germ cell ovarian cancer are curable, 
							even if diagnosed at a late stage, as it usually responds well to treatment.
						</p>
						<p>
							Stromal ovarian cancer develops from connective tissue cells (the cells that fill the 
							ovary and produce hormones). This type of cancer is rare.
						</p>
						<p>
							The treatments and outlook (prognosis) are different for each type of ovarian cancer.
						</p>
						<h4>What causes epithelial ovarian cancer?</h4>
						<p>
							A cancerous (malignant) tumour starts from one abnormal cell. The exact reason why 
							a cell becomes cancerous is unclear. It is thought that something damages or 
							alters certain genes in the cell. This makes the cell abnormal and multiply 
							out of control. (See separate leaflet called What Causes Cancer? for more details.)
						</p>
						<p>
							In most cases, the reason why an ovarian cancer develops is not known. However, 
							there are factors which are known to alter the risk of ovarian cancer developing. These include:
						</p>
						<ul>
							<li>Age. Most cases occur in women over the age of 50 years</li>
							<li>
								Ovulation factors. Factors that reduce the number of times 
								a woman will ovulate slightly lower the risk. For example, taking the oral 
								contraceptive pill, having children and breast-feeding. In contrast, 
								not having children and having a late menopause slightly increase the risk.
							</li>
							<li>Being overweight or obese increases the risk.</li>
							<li>Taking hormone replacement therapy (HRT) may slightly increase the risk.</li>
							<li>Sterilisation surgery or removal of the uterus (hysterectomy) appears to reduce the risk slightly.</li>
							<li>Genetic factors - see below.</li>
						</ul>
						<h4>What are the symptoms of epithelial ovarian cancer?</h4>
						<p>
							In many cases, no symptoms develop for quite some time after the cancer first 
							develops. Symptoms may only be noticed when the cancerous tumour has become quite 
							large. As the tumour grows, the most common early symptoms include one or 
							more of the following.
						</p>
						<ul>
							<li>Constant pain or a feeling of pressure in the pelvic area (lower abdomen).</li>
							<li>Bloating in the abdomen that does not go away (not bloating that comes 
								and goes). There may also be an actual increase in size of your abdomen.</li>
							<li>Difficulty eating, and feeling full quickly.</li>
						</ul>
						<p>Other symptoms that may develop include:</p>
						<ul>
							<li>Loss of appetite.</li>
							<li>Weight loss.</li>
							<li>Pain in the lower abdomen when having sex.</li>
							<li>Passing urine frequently (as the bladder is irritated by the nearby tumour).</li>
							<li>Change in bowel habit such as constipation or diarrhoea.</li>
						</ul>
						<p>
							A more marked swelling of the abdomen. This is caused by ascites, which is a 
							collection of fluid in the abdomen. It is caused by the growth and spread of 
							the cancer to the inside of the abdomen which causes fluid to accumulate.
						</p>
						<p>
							All of the above symptoms can be caused by various other conditions. Also, 
							when symptoms first start they are often vague for some time, such as mild 
							discomfort in the lower abdomen. These symptoms may be thought to be due to 
							other conditions. The possibility of ovarian cancer may not be considered 
							for some time until the symptoms get worse.
						</p>
						<p>
							In particular, one condition that is often mistaken for ovarian cancer is 
							irritable bowel syndrome (IBS). But, it is uncommon for IBS to first develop 
							in women over the age of 50. (IBS typically first develops at a younger age - 
							but may persist into later life). So, if you have not had IBS-type symptoms 
							in the past but then develop them aged over 50 then ovarian cancer should be 
							considered. It needs to be ruled out (usually by tests) before making a diagnosis of IBS.
						</p>
						<p>
							If the cancer spreads to other parts of the body, various other symptoms can develop.
						</p>

