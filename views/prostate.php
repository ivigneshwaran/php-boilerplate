							<li>He may find it hard to start urinating</li>
							<li>He may find it hard to keep urinating once he has started</li>
							<li>He may find it hard to keep urinating once he has started</li>
							<li>Urination might be painful</li>
							<li>Ejaculation may be painful (less common)</li>
							<li>Achieving or maintaining an erection may be difficult (less common)</li>
						</ul>
