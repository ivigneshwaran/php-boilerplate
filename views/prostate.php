						<h2>Prostate</h2>
						<p>
							The prostate is an exocrine gland of the male reproductive system, and exists 
							directly under the bladder, in front of the rectum. An exocrine gland is one whose 
							secretions end up outside the body e.g. prostate gland and sweat glands. It is 
							approximately the size of a walnut.
						</p>
						<p>
							<strong>The urethra</strong> - a tube that goes from the bladder to the end of the penis 
							and carries urine and semen out of the body - goes through the prostate.
						</p>
						<p>
							There are thousands of tiny glands in the prostate - they all produce a fluid 
							that forms part of the semen. This fluid also protects and nourishes the sperm. 
							When a male has an orgasm the seminal-vesicles secrete a milky liquid in which the 
							semen travels. The liquid is produced in the prostate gland, while the sperm is 
							kept and produced in the testicles. When a male climaxes (has an orgasm) contractions 
							force the prostate to secrete this fluid into the urethra and leave the body through the penis.
						</p>
						<h4>Prostate Cancer</h4>
						<p>
							In the vast majority of cases, the prostate cancer starts in the gland cells - 
							this is called adenocarcinoma. In this article, prostate cancer refers just to adenocarcinoma.
						</p>
						<p>
							Prostate cancer is mostly a very slow progressing disease. In fact, many 
							men die of old age, without ever knowing they had prostate cancer - it is only when an 
							autopsy is done that doctors know it was there. Several studies have indicated that perhaps about 
							80% of all men in their eighties had prostate cancer when they died, but nobody knew, not even the doctor.
						</p>
						<p>
							Experts say that prostate cancer starts with tiny alterations in the shape and size of the 
							prostate gland cells - Prostatic intraepithelial neoplasia (PIN). According to Medilexicon`s 
							medical dictionary, Prostatic intraepithelial neoplasia means "dysplastic changes involving 
							glands and ducts of the prostate that may be a precursor of adenocarcinoma; low grade (PIN 1), 
							mild dysplasia with cell crowding, variation in nuclear size and shape, and irregular cell 
							spacing; high grade (PIN 2 and 3), moderate to severe dysplasia with cell crowding, nucleomegaly 
							and nucleolomegaly, and irregular cell spacing."
						</p>
						<h4>Classification of prostate cancer</h4>
						<p>
							It is important to know the stage of the cancer, or how far it has spread. Knowing the 
							cancer stage helps the doctor define prognosis - it also helps when selecting which therapies 
							to use. The most common system today for determining this is the TNM (Tumor/Nodes/Metastases). 
							This involves defining the size of the tumor, how many lymph nodes are involved, and whether 
							there are any other metastases.
						</p>
						<p>
							When defining with the TNM system, it is crucial to distinguish between cancers that are still 
							restricted just to the prostate, and those that have spread elsewhere. Clinical T1 and T2 cancers 
							are found only in the prostate, and nowhere else, while T3 and T4 have spread outside the prostate.
						</p>
						<p>
							There are many ways to find out whether the cancer has spread. Computer tomography will check for 
							spread inside the pelvis, bone scans will decide whether the cancer has spread to the bones, and 
							endorectal coil magnetic resonance imaging will evaluate the prostatic capsule and the seminal vesicles.
						</p>
						<h4>symptoms of prostate cancer</h4>
						<p>
							During the early stages of prostate cancer there are usually no symptoms. Most men at this 
							stage find out they have prostate cancer after a routine check up or blood test. When symptoms 
							do exist, they are usually one or more of the following:
						</p>
						<ul>
							<li>The patient urinates more often</li>
