						<h2>Sentinel lymph node biopsy</h2>
						<p>
							A sentinel lymph node biopsy (SLNB) is a procedure that helps to identify if 
							cancer is in your lymphatic system (the system in your body that produces and 
							stores cells that fight infection and disease).
						</p>
						<p>
							Lymph nodes (or glands) are part of your body's immune system, called the 
							lymphatic system. Lymph nodes are found throughout your body, particularly 
							in your armpits, neck and groin. Their main function is to help fight infection 
							and filter lymphatic fluid. The sentinel lymph node is the first local lymph 
							node that cancer cells are most likely to spread to from a cancer.
						</p>
						<p>The process of finding out if cancer has spread is called staging</p>
						<p>
							In an SLNB, your surgeon will find your sentinel node and remove it. It will 
							then be examined in a laboratory for cancer cells (sometimes your surgeon will 
							remove more than one node).
						</p>
						<p>
							If cancer cells are found in your sentinel node, you may need to have further 
							surgery. This aims to remove all the lymph nodes in that area of your body (lymphadenectomy). 
							You may need other treatment, such as radiotherapy or chemotherapy. If no cancer 
							cells are found, it means the cancer probably hasn't spread so you shouldn’t need 
							to have any of your other lymph nodes removed.
						</p>
						<h4>What are the alternatives?</h4>
						<p>
							To stage the cancer (find out if it's spread) and plan appropriate treatment, 
							it can sometimes be useful if your surgeon checks your lymph nodes closest to 
							your tumour for cancer cells.
						</p>
						<p>
							Depending on the size and location of the cancer you have, your surgeon may 
							suggest you have some or all of your nearby lymph nodes removed. This is done 
							at the same time as you have the primary tumour removed. For example, in some 
							types of breast cancer, your surgeon will remove the lymph nodes in your 
							armpit at the same time as your breast tumour.
						</p>
						<h4>Preparing for sentinel lymph node biopsy</h4>
						<p>
							Your surgeon will explain how to prepare for your procedure. For example, if you 
							smoke, you will be asked to stop as smoking increases your risk of getting a 
							chest and wound infection. This can slow your recovery.
						</p>
						<p>
							SLNB is usually done as a day-case procedure. This means you have the procedure and 
							go home the same day. If you're having a tumour removed at the same time, you may 
							need to stay in hospital for longer. The exact amount of time will depend on the 
							type of cancer and operation you have – ask your surgeon for information.
						</p>
						<p>
							SLNB may be done under general or local anaesthesia. General anaesthesia means you 
							will be asleep during the procedure. Local anaesthesia completely blocks the pain 
							from the biopsy area and you will stay awake during the procedure. You may be offered 
							a sedative with a local anaesthetic. This relieves anxiety and helps you to relax. 
							Your surgeon and anaesthetist will discuss which type of anaesthesia is most suitable for you.
						</p>
						<p>
							If you're having a general anaesthetic, you will be asked to follow fasting 
							instructions. This means not eating or drinking, typically for about six hours 
							beforehand. However, it's important to follow your anaesthetist's advice.
						</p>
						<p>
							Your surgeon will discuss with you what will happen before, during and after your 
							procedure, and any pain you might have. This is your opportunity to understand what 
							will happen. You can help yourself by preparing questions to ask about the risks, 
							benefits and any alternatives to the procedure. This will help you to be informed, 
							so you can give your consent for the procedure to go ahead. You may be asked to do 
							this by signing a consent form.
						</p>
						

