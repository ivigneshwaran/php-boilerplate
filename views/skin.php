						<h2>Skin</h2>
						<p>
							The skin is divided into two main layers: the outer layer known as the epidermis 
							and a layer underneath called the dermis.
						</p>
						<p>
							Skin cancer is the uncontrolled growth of abnormal skin cells. It occurs when 
							unrepaired DNA damage to skin cells (most often caused by ultraviolet radiation 
							from sunshine or tanning beds) triggers mutations, or genetic defects, that lead 
							the skin cells to multiply rapidly and form malignant tumors. Normal skin.
						</p>
						<p>The skin is the largest organ in your body. It does many different things, such as:</p>
						<ul>
							<li>Covering the internal organs and helps protect them from injury</li>
							<li>Serving as a barrier to germs such as bacteria</li>
							<li>Preventing the loss of too much water and other fluids</li>
							<li>Helping control body temperature</li>
							<li>Protecting the rest of the body from ultraviolet (UV) rays</li>
							<li>Helping the body make vitamin D</li>
						</ul>
						<h4>Epidermis</h4>
						<p>
							The top layer of skin is the epidermis. The epidermis is very thin, averaging only 
							about 1/100 of an inch thick. It protects the deeper layers of skin and the organs of 
							the body from the environment.
						</p>
						<p>
							Keratinocytes are the main cell type of the epidermis. These cells make an important 
							protein called keratin that helps the skin protect the rest of the body.
						</p>
						<p>
							The outermost part of the epidermis is called the stratum corneum. It’s composed of 
							dead keratinocytes that are constantly shed as new ones form. The cells in this layer 
							are called squamous cells because of their flat shape.
						</p>
						<p>
							Living squamous cells are found below the stratum corneum. These cells have moved 
							here from the lowest part of the epidermis, the basal layer. The cells of the basal 
							layer, called basal cells, constantly divide to form new keratinocytes. These replace 
							the older keratinocytes that wear off the skin’s surface.
						</p>
						<p>
							Melanocytes, the cells that can become melanoma, are also found in the epidermis. 
							These skin cells make a brown pigment called melanin, which gives the skin its tan or 
							brown color. Melanin protects the deeper layers of the skin from some of the harmful 
							effects of the sun. For most people, when skin is exposed to the sun, melanocytes make 
							more of the pigment, causing the skin to tan or darken.
						</p>
						<p>
							The epidermis is separated from the deeper layers of skin by the basement membrane. 
							This is an important structure because when a skin cancer becomes more advanced, it 
							generally grows through this barrier and into the deeper layers.
						</p>
						<h4>Tumors that start in melanocytes</h4>
						<p>
							A mole (nevus) is a benign skin tumor that develops from melanocytes. Almost 
							everyone has some moles. Nearly all moles (nevi) are harmless, but having some 
							types can raise your risk of melanoma. See the section “What are the risk factors 
							for melanoma skin cancer?” for more information about moles.
						</p>
						<p>
							A Spitz nevus is a kind of mole that sometimes looks like melanoma. It is more 
							common in children and teens, but it can also be seen in adults. These tumors are 
							generally benign and don’t spread. But sometimes doctors have trouble telling Spitz 
							nevi from true melanomas, even when looking at them under a microscope. Therefore, 
							they are often removed, just to be safe.
						</p>
						<h4>Other benign tumors</h4>
						<p>Benign tumors that develop from other types of skin cells include:</p>
						<ul>
							<li>Seborrheic keratoses: tan, brown, or black raised spots with a “waxy” texture</li>
							<li>Hemangiomas: benign blood vessel growths often called cherry or strawberry spots, or port wine stains</li>
							<li>Lipomas: soft growths made up of fat cells</li>
							<li>Warts: rough-surfaced growths caused by a virus</li>
						</ul>
						
