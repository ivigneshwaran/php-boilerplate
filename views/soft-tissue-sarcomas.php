						<h2>Soft Tissue Sarcoma</h2>
						<p>
							Sarcomas are rare cancers that develop in the muscle, bone, nerves, cartilage, 
							tendons, blood vessels and the fatty and fibrous tissues.
						</p>
						<p>
							Soft tissue sarcomas are cancerous (malignant) tumors that originate in the soft 
							tissues of your body. Soft tissues connect, support and surround other body structures. 
							The soft tissues include muscle, fat, blood vessels, nerves, tendons and the lining 
							of your joints (synovial tissues). A large variety of soft tissue sarcomas can occur 
							in these areas.
						</p>
						<p>
							Soft tissue sarcomas aren't common. But soft tissue sarcomas are very serious, 
							especially if diagnosed when the disease is more advanced.
						</p>
						<p>
							Although there are various types of soft tissue sarcoma, they generally share 
							similar characteristics, produce similar symptoms and are treated in similar ways.
						</p>
						<h4>Symptoms</h4>
						<p>
							A soft tissue sarcoma usually produces no signs and symptoms in its early 
							stages. As the tumor grows, it may cause:
						</p>
						<ul>
							<li>A noticeable lump or swelling.</li>
							<li>Pain, if it presses on nerves or muscles.</li>
							<li>
								A blockage in the stomach or intestines or gastrointestinal bleeding if the tumor 
								is located in the abdomen or digestive tract.
							</li>
							<li>
								Soft tissue sarcomas can occur anywhere in your body, but the most common types 
								of soft tissue sarcomas are gastrointestinal stromal tumors and soft tissue sarcomas 
								that affect the extremities. About 60 percent of soft tissue sarcomas occur in the 
								arms, legs, buttocks, hands or feet. Another 20 percent occur in the chest and abdomen. 
								About 10 percent are found in the head and neck.
							</li>
						</ul>
						<p>
							Soft tissue sarcomas go by a variety of names, depending on the tissue in which they 
							originate. Examples of some sarcomas and their locations include:
						</p>
						<ul>
							<li>
								<strong>Rhabdomyosarcoma</strong> More common in children, 
							this sarcoma occurs in the skeletal muscles.
							</li>
							<li>
								<strong>Leiomyosarcoma</strong> Occurs in the smooth muscles â€” muscles not under 
								voluntary control. Found most commonly in the uterus, gastrointestinal tract or 
								lining of blood vessels.
							</li>
							<li>
								<strong>Hemangiosarcoma</strong> Affects blood vessels, especially in areas that 
								have previously received radiation treatment.
							</li>
							<li>
								<strong>Kaposi's sarcoma</strong> A malignancy that occurs in blood vessel 
								walls. Often affects people with immune deficiencies, such as HIV/AIDS.
							</li>
							<li>
								<strong>Lymphangiosarcoma</strong> Affects the lymph vessels and is sometimes 
								seen in a limb with chronic swelling (lymphedema). This can be from an area of
								 prior radiation therapy or certain rare chronic infections.
							</li>
							<li>
								<strong>Synovial sarcoma</strong> Tissue around joints such as knees and 
								ankles are affected. Typically occurs in children and young adults.
							</li>
							<li><strong>Neurofibrosarcoma</strong> Occurs in the peripheral nerves.</li>
							<li><strong>Liposarcoma</strong> Fatty tissue, often in your legs and trunk, is affected.</li>
							<li>
								<strong>Fibrosarcoma</strong> Fibrous tissue in your arms, legs or trunk may be affected.
							</li>
							<li>
								<strong>Malignant fibrous histiocytoma</strong> A fibrous tissue tumor more likely to occur 
								in the legs.
							</li>
							<li>
								<strong>Dermatofibrosarcoma</strong> Grows in the tissue beneath your skin, and often 
								develops in your trunk or limbs
							</li>
						</ul> 
						<h4>Causes</h4>
						<p>
							Generally, the cause of most soft tissue sarcomas is unknown. One of the few 
							soft tissue sarcomas that has a known cause is Kaposi's sarcoma. It occurs in 
							people with defective immune systems and is caused by a virus known as human herpes virus 8 (HHV-8).
						</p>
						<p>Additionally, in some cases, sarcomas may be hereditary, such as in:</p>
						<ul>
							<li>
								Basal cell nevus syndrome. In addition to an increased risk of basal cell skin 
								cancer, people with this disorder are more likely to develop rhabdomyosarcoma or 
								fibrosarcoma. It's caused by a genetic defect.
							</li>
							<li>
								Inherited retinoblastoma. This rare form of childhood eye cancer may increase a 
								child's risk of soft tissue sarcoma and is due to inheritance of a mutated retinoblastoma gene.
							</li>
							<li>
								Li-Fraumeni syndrome. This condition is characterized by an increased risk of 
								many malignancies, including sarcomas, breast cancer, brain cancer and others. 
								People with this syndrome appear to be particularly susceptible to the side effects of radiation therapy.
							</li>
							<li>
								Gardner's syndrome. This hereditary disease leads to precancerous and cancerous 
								growths in the intestines and abdomen.
							</li>
							<li>
								Neurofibromatosis. This condition results in developmental changes in the nervous 
								system, causing nerve sheath tumors. About 1 in 20 people with neurofibromatosis develops malignant tumors.
							</li>
							<li>
								Tuberous sclerosis. Benign tumors, seizures and learning problems are common 
								with this disease, as is an increased risk of rhabdomyosarcoma.
							</li>
							<li>
								Werner's syndrome. A defect in the gene RECQL2 causes numerous health problems, 
								including an increased risk of soft tissue sarcomas.
							</li>
						</ul>
						<h4>Radiation exposure</h4>
						<p>
							Radiation is occasionally associated with sarcomas. These have usually occurred as a 
							side effect of radiation therapy given for other cancers, such as breast cancer or lymphoma. 
							Such serious side effects may become less common as radiation therapy offers more refined 
							dosing strategies and more precise tumor targeting.
						</p>
						<h4>Chemical exposure</h4>
						<p>
							Another factor that may increase your risk of soft tissue sarcoma is 
							exposure to high doses of chemicals such as:
						</p>
						<ul>
							<li>Vinyl chloride, used in making plastics</li>
							<li>Dioxin, an unwanted byproduct of incineration</li>
							<li>Herbicides that contain the chemical phenoxyacetic acid</li>
						</ul>
						
