						<h2>Stomach</h2>
						<p>
							The stomach is is in the upper abdomen. It is part of the gastrointestinal tract (the gut). 
							It lies in the upper part of the abdomen, just below the ribs. When we eat, food passes down 
							the oesophagus (gullet) into the stomach.
						</p>
						<p>
							The stomach makes acid and some enzymes (chemicals) which help to digest food. The muscles 
							in the wall of the stomach contract to mix up the food with the acid and enzymes.
						</p>
						<p>
							Food then passes into the duodenum (the first part of the small intestine). Here food mixes 
							with more enzymes which come from the pancreas and lining of the gut. The enzymes digest (break down) the food.
						</p>
						<p>Digested food is then absorbed into the body from the small intestine.</p>
						<h4>Adenocarcinoma of the stomach</h4>
						<p>
							an most cases, stomach cancer begins from a cell which is on the inside lining of the 
							stomach (the mucosa). This type of stomach cancer is called adenocarcinoma of the stomach. 
							As the cancer cells multiply:
						</p>
						<p>
							The tumour may invade deeper into the wall of the stomach. In time, it may pass through the 
							wall of the stomach and invade nearby organs such as the pancreas or liver.
						</p>
						<p>
							The tumour may spread up or down the stomach into the oesophagus (gullet) or small intestine.
						</p>
						<p>
							Some cells may break off into the lymph channels or bloodstream. The cancer may then spread 
							to nearby lymph nodes or spread to other areas of the body (metastasise).
						</p>
						<h4>Other types of stomach cancer</h4>
						<p>
							There are some less common and rare types of stomach cancer which include:
						</p>
						<ul>
							<li><strong>Lymphomas.</strong> These are cancers which arise from the lymphatic tissue within the wall of the stomach</li>
							<li><strong>Sarcomas.</strong> These are cancers which arise from the muscle or connective tissue within the wall of the stomach</li>
							<li><strong>Carcinoid cancers.</strong> These are cancers which arise from cells in the stomach lining which make hormones</li>
						</ul>
						<p>The rest of this leaflet only discusses adenocarcinoma of the stomach.</p>
						<p>What causes stomach cancer (adenocarcinoma of the stomach)?</p>
						<p>
							A cancerous tumour starts from one abnormal cell. The exact reason why a cell becomes 
							cancerous is unclear. It is thought that something damages or alters certain genes in the 
							cell. This makes the cell abnormal and multiply out of control. (See separate leaflet called 
							'Cancer - What Causes Cancer?' for more details.).
						</p>
						<p>
							Many people develop stomach cancer for no apparent reason. However, certain risk factors 
							increase the chance that stomach cancer may develop. These include:
						</p>
						<p>Ageing. Stomach cancer is more common in older people. Most cases are in people over the age of 55.</p>
						<p>
							Having a type of anaemia called pernicious anaemia, which causes a lack of vitamin B12, 
							can very slightly increase your risk of stomach cancer.
						</p>
						<h4>Diet is probably a factor:</h4>
						<p>Countries such as Japan, where people eat a lot of salt, pickled and smoked foods, have a high rate of stomach cancer.</p>
						<p>Eating a lot of fruit and green vegetables can reduce the risk.</p>
						<p>Smokers have a higher rate of stomach cancer compared with people who do not smoke</p>
						<p>
							Long-term infection of the stomach lining with a bacterium called Helicobacter pylori 
							seems to lead to a slightly higher risk of stomach cancer. (This infection is very common 
							in the UK, and most people with H. pylori infection do not develop stomach cancer. 
							See separate leaflet called 'Helicobacter Pylori and Stomach Pain'.).
						</p>
						<p>Gender. Stomach cancer is twice as common in men as in women.</p>
						<p>If you have had part of your stomach removed in the past for any reason. For example, 
							to treat a stomach ulcer or some other condition.
						</p>
						<p>
							Family history. For some cases, stomach cancer may run in the family. However, most cases of 
							stomach cancer do not run in families and are not inherited.
						</p>
						<p>Blood group A. People who have this blood group have a slightly higher risk.</p>
						<h4>What are the symptoms of stomach cancer?</h4>
						<p>
							When a stomach cancer first develops and is small, it usually causes no symptoms. 
							Some do not cause symptoms until they are quite advanced. Initial symptoms may include:
						</p>
						<ul>
							<li>Pain or discomfort in the upper abdomen, especially after eating</li>
							<li>Indigestion. (Note: most people who have indigestion do not have stomach cancer.)</li>
							<li>Feeling sick, off food. Some people have a sense of fullness after eating.</li>
							<li>Weight loss and/or loss of appetite.</li>
						</ul>
						<p>
							You may pass blood out with your faeces (motions). You may not notice bleeding 
							if small amounts of blood pass out with your faeces. A lot of bleeding from the 
							stomach can turn your faeces black.
						</p>
						<p>As the cancer grows in the stomach, symptoms may become worse and may include:</p>
						<ul>
							<li>The same symptoms as above, but more severe</li>
							<li>Feeling generally unwell and more tired than usual</li>
							<li>Becoming anaemic if the tumour regularly bleeds. This can cause you to become more tired than usual</li>
							<li>The cancer growing very large and causing a blockage to food and drink</li>
							<li>If the cancer spreads to other parts of the body, various other symptoms can develop</li>
						</ul>
						<p>
							Note: all the above symptoms can be due to other conditions, so tests are needed to 
							confirm stomach cancer.
						</p>

