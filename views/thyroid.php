						<h2>Thyroid Cancer</h2>
						<p>
							Thyroid cancer is not a very common type of cancer. There are different types of thyroid 
							cancer which have different treatments.
						</p>
						<p>
							The thyroid is a small gland in the neck. It is made of two lobes or parts, shaped 
							like a butterfly. You cannot usually feel the thyroid in the neck if it is normal.
						</p>
						<p>
							The thyroid gland contains cells called follicular cells. These make two main hormones, 
							thyroxine (T4) and triiodothyronine (T3), which are needed to keep the body functioning 
							at its normal rate. These hormones affect the heart rate, body temperature and energy 
							level. The thyroid gland also contains C cells which make calcitonin, a hormone that 
							helps to control the level of calcium in the blood.
						</p>
						<h4>Thyroid cancer</h4>
						<p>There are four main types of thyroid cancer</p>
						<p>
							<strong>Papillary.</strong> This is the most common type of thyroid cancer
						</p>
						<p>
							<strong>Follicular.</strong> This is a less common type of thyroid cancer, usually found in older 
							people. Both papillary and follicular thyroid cancers are sometimes called differentiated 
							thyroid cancer. They are often treated in the same way.
						</p>
						<p>
							<strong>Medullary.</strong> This is a rare type of thyroid cancer that can run in families. 
							For this reason, family members may be checked at regular intervals to ensure they are not 
							showing any signs of the cancer. This grows from the C cells in the thyroid gland.
						</p>
						<p>
							<strong>Anaplastic.</strong> This is also rare. It occurs more commonly in older people and 
							grows quickly. Unlike other types of thyroid cancer, it can be difficult to treat.
						</p>
						<p>
							The other types of cancer that can develop in the thyroid gland are 
							lymphoma and HÃ¼rthle cell cancer. These will not be discussed in this article.
						</p>
						<p>
							Papillary and follicular thyroid cancers account for 80-90% of all thyroid cancers. 
							Both types begin in the follicular cells of the thyroid. Most papillary and follicular 
							thyroid cancers tend to grow slowly. If they are detected early, most can be treated successfully.
						</p>
						<h4>Thyroid cancer</h4>
						<p>
							A cancerous tumour starts from one abnormal cell. The exact reason why a cell 
							becomes cancerous is unclear. It is thought that something damages or alters 
							certain genes in the cell. This makes the cell abnormal and multiply out of control.
						</p>
						<p>
							Thyroid cancer is uncommon. Around 1,750 people develop it each year in the UK. 
							Women are more commonly affected than men. Although most people who develop thyroid 
							cancer are middle-aged or older, papillary thyroid cancer can affect younger women, 
							most commonly between the ages of 35 and 40 years.
						</p>
						<p>
							Many people develop thyroid cancer for no apparent reason. However, certain risk 
							factors increase the chance that thyroid cancer may develop. These include:
						</p>
						<p>
							<strong>Thyroid diseases.</strong> People who have some non-cancerous (benign) 
							thyroid diseases are more likely to develop thyroid cancer. For example, a goitre 
							(enlarged thyroid), thyroid nodules (adenomas), or inflammation of the thyroid 
							(thyroiditis). Note: having hypothyroidism (underactive thyroid) or hyperthyroidism 
							(overactive thyroid) does not increase your risk of developing thyroid cancer.
						</p>
						<p>
							<strong>Previous radiation.</strong> Thyroid cancer is more common in people who 
							had radiotherapy treatment to the neck area at a younger age.
						</p>
						<p>
							<strong>Family history.</strong> Medullary thyroid cancer can be caused by 
							inheriting abnormal genes. Around one in four people who develop medullary 
							thyroid cancer have an abnormal gene.
						</p>
						<p>Low iodine levels. However, it is very rare for people in the UK to have low iodine levels</p>
						<h4>Symptoms of thyroid cancer</h4>
						<p>
							Thyroid cancer usually develops slowly and initially does not cause any symptoms. 
							The most common first sign is a small lump in the neck, which is painless. 
							Other symptoms which may develop as the cancer grows include:
						</p>
						<ul>
							<li>Hoarseness or difficulty in speaking in a normal voice.</li>
							<li>Swollen lymph glands in the neck.</li>
							<li>Difficulty swallowing or breathing as the cancer presses on the oesophagus (gullet) or windpipe.</li>
							<li>Pain in the throat or neck</li>
						</ul>
						<p>Note: most lumps in the thyroid gland are not due to cancer. Only about 1 in 20 thyroid lumps are due to cancer.</p>

						
