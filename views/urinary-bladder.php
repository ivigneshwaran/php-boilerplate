						<h2>Bladder Cancer</h2>
						<p>
							The bladder is part of the urinary tract. It is at the bottom of the abdomen. 
							It fills with urine and we pass urine out from time to time through a tube called the 
							urethra. The urethra passes through the prostate gland and penis in men. The urethra is 
							shorter in women and opens just above the vagina.
						</p>
						<p>
							Urine is made in the kidneys and contains water and waste materials. A tube called a 
							ureter comes from each kidney and drains the urine into the bladder.
						</p>
						<p>
							The cells that line the inside of the bladder are called transitional cells or urothelial cells. 
							There is a thin layer of cells beneath the lining, called the lamina propria.
						</p>
						<p>
							The outer part of the bladder wall contains a thick layer of muscle tissue which contracts 
							from time to time to push out the urine.
						</p>
						<h4>What is cancer</h4>
						<p>
							Cancer is a disease of the cells in the body. The body is made up from millions of tiny 
							cells. There are many different types of cell in the body, and there are many different 
							types of cancer which arise from different types of cell. What all types of cancer have in 
							common is that the cancer cells are abnormal and multiply out of control.
						</p>
						<p>
							A malignant tumour is a lump of tissue made from cancer cells which continue to multiply. 
							Malignant tumours can invade into nearby tissues and organs, which can cause damage.
						</p>
						<p>
							Malignant tumours may also spread to other parts of the body. This happens if some cells 
							break off from the first (primary) tumour and are carried in the bloodstream or lymph channels 
							to other parts of the body. These small groups of cells may then multiply to form secondary 
							tumours (metastases) in one or more parts of the body. These secondary tumours may then grow, 
							invade and damage nearby tissues, and spread again.
						</p>
						<p>
							Some cancers are more serious than others; some are more easily treated than others 
							(particularly if diagnosed at an early stage); some have a better outlook (prognosis) 
							than others.
						</p>
						<p>
							So, cancer is not just one condition. In each case it is important to know exactly what type of 
							cancer has developed, how large it has become, and whether it has spread. This will enable you to 
							get reliable information on treatment options and outlook. (See separate leaflet called 'Cancer -
							 What are Cancer and Tumours?' for general details about cancer.)
						</p>
						<h4>What is bladder cancer and how common is it?</h4>
						<p>Transitional cell bladder cancer is divided into two groups:</p>
						<p>
							<strong>Superficial tumours.</strong> These occur in about 4 in 5 cases. These tumours are 
							confined to the inner lining, or just below the inside lining, of the bladder. Sometimes the 
							cells which form this type of cancer multiply to form little growths which stick out like warts 
							from the inside lining of the bladder.
						</p>
						<p>
							<strong>Muscle invasive tumours.</strong> These occur in about 1 in 5 cases. These tumours 
							have spread to the muscle layer of the bladder, or right through the wall of the bladder.
						</p>
						<p>
							The treatment and outlook for each of these two groups are very different. Superficial tumours 
							rarely spread and can usually be cured. However, if left untreated, in some cases they can develop 
							into muscle invasive tumours. Muscle invasive tumours have a high chance of spreading to other parts 
							of the body (metastasise), and treatment has less chance of being curative.
						</p>
						<h4>What causes bladder cancer?</h4>
						<p>
							A cancerous tumour starts from one abnormal cell. The exact reason why a cell becomes 
							cancerous is unclear. It is thought that something damages or alters certain genes in the 
							cell. This makes the cell abnormal and multiply out of control. (See separate leaflet 
							called 'Cancer - What Causes Cancer?' for more detail.)
						</p>
						<p>
							In many cases, the reason why a bladder cancer develops is not known. However, 
							there are factors which are known to alter the risk of bladder cancer developing. These include:
						</p>
						<ul>
							<li>Increasing age. Most bladder cancers occur in people over the age of 50. It is rare in people aged younger than 40.</li>
							<li>
								Smoking. Bladder cancer is four times more common in smokers than non-smokers. Some of 
								the chemicals from tobacco get into the body and are passed out in urine. These chemicals 
								in the urine are carcinogenic (damaging) to the bladder cells. It is estimated that about 
								one third of bladder cancers are related to smoking.
							</li>
							<li>
								Other chemicals. Certain workplace and environmental chemicals have been linked to bladder 
								cancer - for example, substances used in the rubber and dye industries. Many of these chemicals 
								are now banned in the UK. However, bladder cancer may develop as late as 10-25 years after exposure 
								to certain chemicals. This means that some cases are still being diagnosed in people who 
								worked with these chemicals years ago.
							</li>
							<li>
								Gender.Bladder cancer is about three times more common in men than women.
							</li>
							<li>Ethnic background.Bladder cancer is more common in white people than in black people.</li>
							<li>
								Food and drink. People who eat plenty of fruit and vegetables have a lower risk of developing 
								bladder cancer than those who do not. Also, people who drink a lot of coffee have a slightly increased risk.
							</li>
							<li>Previous radiotherapy or chemotherapy increases the risk.</li>
							<li>Schistosomiasis. This bladder infection, which is caused by a parasite in certain hot countries, increases the risk.</li>
							<li>Repeated bouts of other types of bladder infection may also slightly increase the risk.</li>
						</ul>
						<h4>Symptoms of Bladder Cancer</h4>
						<h4>Blood in urine</h4>
						<p>
							In most cases, the first symptom is to pass blood in the urine (haematuria). Haematuria 
							caused by an early bladder tumour is usually painless. You should always see your doctor 
							if you pass blood in your urine. The blood in the urine may come and go as the tumour bleeds 
							from time to time.
						</p>
						<h4>Other symptoms</h4>
						<p>
							Some tumours may cause irritation of the bladder and cause symptoms similar to a urine infection. 
							For example, passing urine frequently or pain on passing urine. If the cancer is a muscle-invasive type, 
							and grows through the wall of the bladder, then other symptoms may develop over time. For example, pain 
							in the lower abdomen.
						</p>
						<p>If the cancer spreads to other parts of the body, various other symptoms can develop.</p>
						


