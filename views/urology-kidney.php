						<h2>Kidney Cancer</h2>
						<p>
							The two kidneys lie to the sides of the upper part of the tummy (abdomen), behind the 
							intestines, and either side of the spine. Each kidney is about the size of a large 
							orange, but bean-shaped.
						</p>
						<p>
							The kidneys are a pair of reddish-brown bean-shaped organs, each about the size of a 
							small fist, that are located above the waist on either side of the spine. They are closer 
							to the back of the body than to the front. The kidneys filter blood and remove impurities, 
							excess minerals and salts, and extra water. Every day, the kidneys filter about 200 quarts 
							of blood to generate two quarts of wastewater (urine).
						</p>
						<p>
							The kidneys also produce hormones that help control blood pressure, red 
							blood cell production, and other functions. Although most people have two kidneys, 
							each works independently, which means the body can function with less than one complete 
							kidney. With dialysis, a mechanized filtering process, it is possible to live without kidneys.
						</p>
						<h4>Types of kidney cancer</h4
						<p>
							Kidney cancer begins when normal cells in one or both kidneys change and grow 
							uncontrollably, forming a mass called a tumor. A tumor can be benign (noncancerous) or 
							malignant (cancerous, meaning it can spread to other parts of the body).
						</p>
						<p>There are several types of kidney cancer:</p>
						<ul>
							<li>
								<strong>Renal cell carcinoma.</strong> Renal cell carcinoma makes up about 85% of kidney cancers. 
								This cancer develops within the kidney's microscopic filtering systems, the lining of 
								the tiny tubes that lead to the bladder. The treatment options for renal cell carcinoma are 
								discuss later in this guide.</li>
							<li>
								<strong>Transitional cell carcinoma.</strong> This is also called urothelial carcinoma. 
								Transitional cell carcinoma begins in the area of the kidney where urine collects before moving 
								to the bladder. This type of kidney cancer is similar to bladder cancer and is treated like bladder 
								cancer. It accounts for 10% to 15% of the kidney cancers diagnosed in adults.
							</li>
							<li>
								<strong>Sarcoma.</strong> Sarcoma of the kidney is rare and is treated with surgery. 
								For some patients, it may be beneficial to combine chemotherapy with surgery, as sarcoma 
								can grow quite large before it is discovered. It does not metastasize (spread) as often as 
								other types of kidney cancer.
							</li>
							<li>
								<strong>Wilms tumor.</strong> Wilms tumor is most common in children and is treated differently than kidney 
								cancer in adults. This type of tumor is more likely to be successfully treated with radiation 
								therapy and chemotherapy than the other types of kidney cancer, and this has resulted in a different 
								approach to treatment.
							</li>
						</ul>
						<h4>Types of kidney cancer cells</h4>
						<p>
							Knowing which kind of cell a tumor is made of helps doctors plan treatment. There are 
							several types of kidney cancer cells. The most common are listed below. Pathologists 
							(doctors who specialize in interpreting laboratory tests and evaluating cells, tissues, 
							and organs to diagnose disease) have identified as many as 10 different types of these cells.
						</p>
						<p>
							Clear cell is the type of cell that is found in about 70% of kidney cancers. Clear 
							cells range from slow growing (grade 1) to fast growing (grade This type of kidney 
							cancer is particularly responsive to immunotherapy and targeted therapy (see the Treatment 
							Options section).
						</p>
						<p>
							Papillary kidney cancer, which develops in 10% to 15% of patients, is divided into two 
							different subtypes, called type 1 and type 2. They are different from the clear cell type, 
							although papillary kidney cancer is currently treated in the same way as clear cell kidney 
							cancer. However, many doctors recommend treatment through clinical trials because treatment 
							with targeted therapy is often not as successful for people with papillary kidney cancer as 
							it is for people with clear cell kidney cancer.
						</p>
						<p>
							Sarcomatoid is the type of cell that grows the fastest. It may be found with clear cell or 
							papillary type. It is called sarcomatoid because it looks like sarcoma under a microscope.
						</p>
						<p>
							Collecting duct is a rare cancer that behaves in a similar way to transitional cell carcinoma. 
							It is best treated with chemotherapy. However, many doctors believe that it is less responsive 
							to chemotherapy than transitional cell carcinoma but more responsive than clear cell or sarcomatoid types.
						</p>
						<p>Chromophobe is another rare cancer that is different from other types.</p>
						<p>Oncocytoma is a slow-growing type that rarely, if ever, spreads</p>
						<p>
							Angiomyolipoma is a benign tumor that has a unique appearance on a computed tomography (CT 
							or CAT) scan (see the Diagnosis section) and when viewed under a microscope. It tends to be 
							less likely to grow and spread and is best treated with surgery.
						</p>
