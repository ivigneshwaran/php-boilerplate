						<h2>Uterus Cancer</h2>
						<p>
							Most cases of uterine cancer arise from the inside lining of the uterus (the endometrium). 
							This is called endometrial cancer. The most common early symptom is abnormal vaginal bleeding.
						</p>
						<h4>Uterus</h4>
						<p>
							The uterus (womb) is in the lower abdomen behind the bladder. The inside of the uterus 
							is where a baby grows if you become pregnant. The inside lining of the uterus is called 
							the endometrium. This builds up and is then shed each month as a period in women who 
							have not yet gone through the menopause. The thick body of the uterus is called the 
							myometrium and is made of specialised muscle tissue. The lowest part of the uterus 
							is called the cervix (the neck of the womb) which pushes just into the top part of 
							the vagina. At the top of the womb are the right and left Fallopian tubes which carry 
							the eggs (released from the ovaries) to inside the womb.
						</p>
						<h4>uterine cancer</h4>
						<p>
							Most uterine cancers develop from cells in the endometrium (endometrial cancer). 
							Cancer developing from muscle cells in the myometrium (uterine sarcomas) are rare 
							and are not dealt with further in this leaflet. Cervical cancer (cancer of the cervix) 
							is quite different to uterine cancer and is dealt with in a separate leaflet.
						</p>
						<p>The rest of this leaflet deals only with endometrial cancer of the uterus.</p>
						<h4>Endometrial cancer</h4>
						<p>Type and grade of endometrial cancer</p>
						<p>
							Most cases of endometrial cancer are called endometrioid adenocarcinomas. These arise from 
							cells which form the glandular tissue in the lining of the endometrium. A sample of cancer 
							tissue can be looked at under the microscope. By looking at certain features of the cells 
							the cancer can be graded.
						</p>
						<ul>
							<li>
								<strong>Grade 1</strong> (low grade) - the cells look reasonably similar to normal endometrial 
								cells. The cancer cells are said to be well differentiated. The cancer cells 
								tend to grow and multiply quite slowly and are not so aggressive.
							</li>
							<li><strong>Grade 2</strong> - is a middle grade</li>
							<li>
								<strong>Grade 3</strong> - the cells look very abnormal and are said to be poorly differentiated. 
								The cancer cells tend to grow and multiply quite quickly and are more aggressive.
							</li>
						</ul>
						<p>There are also some rarer types of endometrial cancer.</p>
						<h4>What causes endometrial cancer</h4>
						<p>
							A cancerous tumour starts from one abnormal cell. The exact reason why a cell becomes 
							cancerous is unclear. It is thought that something damages or alters certain genes in 
							the cell. This makes the cell abnormal and multiply out of control. See separate leaflet 
							called What causes cancer? for more details.
						</p>
						<p>
							There are risk factors which are known to increase the risk of endometrial cancer 
							developing. These include the following:
						</p>
						<p>
							Increased exposure to oestrogen. Oestrogen is the main female hormone. Before the 
							menopause the changing level of oestrogen together with another hormone, progesterone, 
							cause the endometrium to build up each month and then be shed as a period. It is thought 
							that factors which lead to prolonged higher than usual levels of oestrogen, or increased 
							levels of oestrogen not being balanced by progesterone, may somehow increase the risk
							 of endometrial cells becoming cancerous. These include:
						</p>
						<ul>
							<li>
								If you have never had a baby. This is because your womb has never had 
								a rest from the rise of oestrogen that happens in the course of a normal monthly cycle.
							</li>
							<li>If you are overweight or obese. This is because fat cells make a certain amount of oestrogen.</li>
							<li>If you have certain rare oestrogen-producing tumours.</li>
							<li>
								If you have a late menopause (after the age of 52) or started periods at a young age. 
								This is because you will have more monthly menstrual cycles.
							</li>
							<li>
								Endometrial hyperplasia. This is a benign (non-cancerous) condition where the endometrium 
								builds up more than usual. It can cause heavy periods or irregular bleeding after the menopause. 
								Most women with this condition do not develop cancer, but the risk is slightly increased.
							</li>
							<li>
								Tamoxifen. This is a drug which is used in the treatment of breast cancer. 
								The risk of developing endometrial cancer from tamoxifen is very small - about 1 in 500. 
								However, the benefits of taking tamoxifen usually outweigh the risks.
							</li>
							<li>
								Diabetes. There is a small increased risk in women with diabetes.
							</li>
							<li>
								Polycystic ovary syndrome. There is a very slight increased risk in women with this condition.
							</li>
							<li>
								Diet. There are much fewer cases of endometrial cancer in certain eastern countries 
								and dietary factors may be the reason. A western diet high in fat may be a contributing factor.
							</li>
							<li>
								Genetic factors. Most cases are not due to genetic or hereditary factors. However, 
								in a small number of cases, a faulty gene (which can be inherited) may trigger the 
								disease. This disorder is called hereditary nonpolyposis colon cancer (HNPCC).
							</li>
							<li>
								Women who take the combined oral contraceptive pill have a lower risk of developing endometrial cancer.
							</li>
						</ul>
						<h4>What are the symptoms of endometrial cancer?</h4>
						<p>In most cases the first symptom to develop is abnormal vaginal bleeding such as:</p>
						<ul>
							<li>
								Vaginal bleeding past the menopause. This can range from spotting to more heavy 
								bleeds. This is the most common symptom of endometrial cancer.
							</li>
							<li>Bleeding after having sex (postcoital bleeding).</li>
							<li>
								Bleeding between normal periods (intermenstrual bleeding) in women who have 
								not gone through the menopause.
							</li>
						</ul>
						<p>Early symptoms that occur in some cases are:</p>
						<ul>
							<li>pain during or after having sex</li>
							<li>vaginal discharge.</li>
							<li>pain in the lower abdomen.</li>
						</ul>

